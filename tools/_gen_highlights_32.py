# Fix simplified chars / mixed
RAW = [x.replace("临近", "臨近").replace("区分", "區分").replace("冲动", "衝動") for x in RAW]

assert len(RAW) == 150, len(RAW)

# local validate mirror
NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
forbidden = ("本書", "作者指出", "本章", "這一章")
highlights = [f"{i:03d}、{t}" for i, t in enumerate(RAW, 1)]
bodies = []
short = []
for i, line in enumerate(highlights, 1):
    assert line.startswith(f"{i:03d}、")
    body = NUMBER_RE.sub("", line, 1).strip()
    assert len(body) >= 12
    assert "｜" not in body and "\n" not in body
    assert not any(p in body for p in forbidden)
    m = re.match(r"^([^：:]{1,12})[：:]", body)
    if m and not m.group(1).endswith(NATURAL):
        short.append(i)
    bodies.append(body)
assert len(short) < 3, short
assert len(set(bodies)) == len(bodies)
rep = Counter(b[:18] for b in bodies if len(b) >= 18)
assert rep.most_common(1)[0][1] < 4, rep.most_common(3)

