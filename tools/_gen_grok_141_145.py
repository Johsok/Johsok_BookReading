def write_book(book_id: str, title: str, author: str, bodies: list[str]) -> Path:
    """Validate and write one results JSON for findbook_writer complete."""
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} expected 150, got {len(bodies)}")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]
    validate_highlights(book_id, highlights, title, author)
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"results\t{out}")
    return out


def main() -> None:
    """Write results and run complete for each book."""
    for book_id, (title, author, bodies) in BOOKS.items():
        out = write_book(book_id, title, author, bodies)
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ]
        print("run\t", " ".join(cmd))
        subprocess.run(cmd, check=True, cwd=str(ROOT))


if __name__ == "__main__":
    main()

