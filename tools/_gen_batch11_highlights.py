def numbered(lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise ValueError(f"need 150, got {len(lines)}")
    if len(set(lines)) != 150:
        raise ValueError("duplicate bodies")
    return [f"{i:03d}、{body}" for i, body in enumerate(lines, 1)]


