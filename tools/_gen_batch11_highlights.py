    written: list[str] = []
    failures: list[str] = []

    for book_id, (category_id, bodies) in catalog.items():
        try:
            highlights = numbered(bodies)
            # extra validation mirrors writer rules lightly
            for i, line in enumerate(highlights, 1):
                body = line.split("、", 1)[1]
                if len(body) < 12:
                    raise ValueError(f"line {i} too short: {body}")
            results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
            payload = {"id": book_id, "highlights": highlights}
            results_path.write_text(
                json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
                encoding="utf-8",
            )
            cmd = [
                sys.executable,
                str(ROOT / "tools" / "findbook_writer.py"),
                "complete",
                "--category-id",
                category_id,
                "--results",
                str(results_path),
            ]
            proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
            if proc.returncode != 0:
                raise RuntimeError(
                    (proc.stdout or "") + "\n" + (proc.stderr or "")
                )
            print(proc.stdout.strip())
            written.append(book_id)
        except Exception as exc:  # noqa: BLE001
            msg = f"{book_id}: {exc}"
            print(msg, file=sys.stderr)
            failures.append(msg)

    print("WRITTEN:", ",".join(written))
    print("FAILURES:", len(failures))
    for item in failures:
        print(item)
    return 1 if failures else 0


