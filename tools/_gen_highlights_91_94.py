"""Generate and complete grok highlights for books 91-94."""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = Path(__file__).resolve().parent
sys.path.insert(0, str(TOOLS))
from findbook_writer import validate_highlights  # noqa: E402


def numbered(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150, got {len(bodies)}")
    return [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]


def write_and_complete(book_id: str, title: str, author: str, bodies: list[str]) -> None:
    highlights = numbered(bodies)
    validate_highlights(book_id, highlights, title, author)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    out = (proc.stdout or "") + (proc.stderr or "")
    print(out.strip())
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    print(f"written\t{book_id}\t150\tgrok")


