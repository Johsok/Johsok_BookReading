def write_and_complete(book_id: str) -> None:
    title, author, items = BOOKS[book_id]
    highlights = pack(items)
    validate(book_id, highlights, title, author)
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout or "")
    sys.stderr.write(proc.stderr or "")
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    print(f"DONE\t{book_id}\t{len(highlights)}")


if __name__ == "__main__":
    target = sys.argv[1] if len(sys.argv) > 1 else "01_business_startup-20260717-151"
    write_and_complete(target)
