def validate(book_id: str, bodies: list[str], title: str = "", author: str = "") -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise ValueError(f"{book_id} duplicates")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {starts.most_common(3)}")
    forbidden = ("本書", "作者指出", "本章", "這一章")
    short_colon = []
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short")
        if "｜" in body or "\n" in body or "\r" in body:
            raise ValueError(f"{book_id} #{i} forbidden format")
        if any(p in body for p in forbidden):
            raise ValueError(f"{book_id} #{i} forbidden prefix")
        if "面第" in body and "步" in body:
            raise ValueError(f"{book_id} #{i} step wording")
        import re

        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} #{i} step pattern")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            short_colon.append(i)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short colon lines: {short_colon[:10]}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats full {label}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, bodies: list[str], title: str, author: str) -> None:
    highlights = validate(book_id, bodies, title, author)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout)
    if proc.returncode != 0:
        print(proc.stderr)
        raise SystemExit(proc.returncode)
    print(f"OK {book_id} 150 grok")


