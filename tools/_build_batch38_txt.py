def save(name: str, lines: list[str]) -> None:
    lines = [ln.strip() for ln in lines if ln.strip()]
    if len(lines) != 150:
        raise SystemExit(f"{name}: got {len(lines)}, need 150")
    if len(set(lines)) != 150:
        raise SystemExit(f"{name}: duplicate bodies")
    short = [ln for ln in lines if len(ln) < 12]
    if short:
        raise SystemExit(f"{name}: short lines: {short[:3]}")
    path = ROOT / f"_batch38_{name}.txt"
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {path.name} ({len(lines)})")


