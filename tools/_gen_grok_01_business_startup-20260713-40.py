def main() -> int:
    bodies = [s.replace("不确定", "不確定").replace("稳住", "穩住") for s in RAW]
    if len(bodies) != 150:
        raise SystemExit(f"need 150, got {len(bodies)}")

    number_re = re.compile(r"^\d{3}、")
    forbidden_prefixes = ("本書", "作者指出", "本章", "這一章")
    natural_colon_suffixes = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]
    cleaned_bodies = []
    short_colon = []
    for i, line in enumerate(highlights, 1):
        expected = f"{i:03d}、"
        if not line.startswith(expected):
            raise SystemExit(f"bad number {i}")
        if "\n" in line or "\r" in line or "｜" in line:
            raise SystemExit(f"forbidden format {i}")
        body = number_re.sub("", line, count=1).strip()
        if len(body) < 12:
            raise SystemExit(f"too short {i}: {body}")
        if any(p in body for p in forbidden_prefixes):
            raise SystemExit(f"forbidden prefix {i}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"step wording {i}")
        for bad in ("**", "__", "`", "#"):
            if bad in body:
                raise SystemExit(f"markdown {i}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(natural_colon_suffixes):
            short_colon.append(i)
        cleaned_bodies.append(body)

    if len(short_colon) >= 3:
        raise SystemExit(f"short colon lines: {short_colon}")
    if len(set(cleaned_bodies)) != len(cleaned_bodies):
        raise SystemExit("exact duplicates")
    repeated_starts = Counter(b[:18] for b in cleaned_bodies if len(b) >= 18)
    if repeated_starts and repeated_starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"repeated starts: {repeated_starts.most_common(5)}")
    for label, value in (("書名", TITLE), ("作者", AUTHOR)):
        if value and sum(value in b for b in cleaned_bodies) >= 2:
            raise SystemExit(f"repeated {label}")

    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"wrote\t{out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
