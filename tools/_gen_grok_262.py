def fix_colons(bodies: list[str]) -> list[str]:
    natural = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    out = []
    for b in bodies:
        m = re.match(r"^([^：:]{1,12})[：:]", b)
        if m and not m.group(1).endswith(natural):
            b = re.sub(r"^([^：:]{1,12})[：:]", r"\1，", b, count=1)
        for a, c in [("要对", "要對"), ("初筛", "初篩")]:
            b = b.replace(a, c)
        out.append(b)
    return out


def main() -> None:
    bodies = fix_colons(BODIES)
    print("count", len(bodies))
    if len(bodies) != 150:
        raise SystemExit(f"count={len(bodies)}")
    # avoid author/title leakage
    for bad in (TITLE, AUTHOR, "吳燦銘"):
        hits = sum(bad in b for b in bodies)
        if hits >= 2:
            raise SystemExit(f"leak {bad}: {hits}")
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    validate_highlights(BOOK_ID, highlights, TITLE, AUTHOR)
    out = TOOLS / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    proc = subprocess.run(
        [sys.executable, str(TOOLS / "findbook_writer.py"), "complete", "--category-id", "01_business_startup", "--results", str(out)],
        cwd=str(ROOT), capture_output=True, text=True,
    )
    print(proc.stdout or proc.stderr)
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    saved = json.loads((ROOT / "Books" / "01_business_startup" / f"{BOOK_ID}.json").read_text(encoding="utf-8-sig"))
    print(f"OK\t{BOOK_ID}\t{len(saved['chatgptHighlights'])}\t{saved.get('highlightsSource')}")


if __name__ == "__main__":
    main()
