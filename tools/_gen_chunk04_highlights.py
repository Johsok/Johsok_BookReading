def fix_lang(items: list[str]) -> list[str]:
    """Normalize accidental simplified / mixed tokens."""
    repl = {
        "勇气": "勇氣",
        "街区": "街區",
        "补眠": "補眠",
        "快点": "快點",
        "compar": "比較",
        "overnight": "整晚",
        "ded出": "說出",
        "sparring partner": "陪練對手",
        "心态": "心態",
        "扎实": "紮實",
        "足迹": "足跡",
        "诚实": "誠實",
    }
    out = []
    for s in items:
        for a, b in repl.items():
            s = s.replace(a, b)
        out.append(s)
    return out


