def main() -> None:
    if len(RAW) != 150:
        raise SystemExit(f"expected 150 got {len(RAW)}")
    # Fix remaining English/latin issues that might look odd - validate first
    highlights = [f"{i:03d}、{t}" for i, t in enumerate(RAW, 1)]
    # Replace a few awkward bilingual lines if present
    fixed = []
    for h in highlights:
        h = h.replace("Crowding（擁擠交易）", "擁擠交易")
        h = h.replace("當波動從 latent 變 realized，", "當隱含波動變成已實現波動，")
        h = h.replace("投資哲學要能一句话说清", "投資哲學要能一句話說清")
        fixed.append(h)
    highlights = fixed
    validate_highlights(BOOK_ID, highlights, TITLE, AUTHOR)
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    result = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(result.stdout)
    sys.stderr.write(result.stderr)
    if result.returncode != 0:
        raise SystemExit(result.returncode)
    print(f"OK\t{BOOK_ID}\t{len(highlights)}")


if __name__ == "__main__":
    main()
