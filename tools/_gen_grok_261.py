def fix_colons(bodies: list[str]) -> list[str]:
    natural = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    out = []
    for b in bodies:
        m = re.match(r"^([^：:]{1,12})[：:]", b)
        if m and not m.group(1).endswith(natural):
            b = re.sub(r"^([^：:]{1,12})[：:]", r"\1，", b, count=1)
        # tradify common simplifications
        for a, c in [("作为", "作為"), ("台阶", "臺階"), ("较稳", "較穩"), ("写成", "寫成")]:
            b = b.replace(a, c)
        out.append(b)
    return out


