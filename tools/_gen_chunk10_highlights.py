def fmt(lines: list[str]) -> list[str]:
    out = []
    for i, body in enumerate(lines, 1):
        body = body.strip()
        if len(body) < 12:
            raise ValueError(f"too short [{i}]: {body}")
        out.append(f"{i:03d}、{body}")
    if len(out) != 150:
        raise ValueError(f"need 150 got {len(out)}")
    return out


