def validate(lines: list[str]) -> None:
    if len(lines) != 150:
        raise SystemExit(f"need 150, got {len(lines)}")
    if len(BODIES) != 150:
        raise SystemExit(f"bodies need 150, got {len(BODIES)}")
    short_colon = []
    bodies = []
    for index, line in enumerate(lines, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise SystemExit(f"bad number at {index}")
        if "\n" in line or "\r" in line or "｜" in line or "|" in line:
            raise SystemExit(f"bad format at {index}")
        if any(x in line for x in ("**", "##", "`", "面第", "第1步", "第2步")):
            raise SystemExit(f"markdown/step at {index}")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise SystemExit(f"too short at {index}: {body}")
        if any(p in body for p in FORBIDDEN_PREFIXES):
            raise SystemExit(f"forbidden prefix at {index}")
        if TITLE in body or AUTHOR in body:
            raise SystemExit(f"title/author at {index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"step wording at {index}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(index)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise SystemExit(f"short colon lines: {short_colon}")
    if len(set(bodies)) != len(bodies):
        raise SystemExit("exact duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    bad = [(k, c) for k, c in starts.items() if c >= 4]
    if bad:
        raise SystemExit(f"repeated starts: {bad[:5]}")
    # no English leftover
    for i, b in enumerate(bodies, 1):
        if re.search(r"[A-Za-z]{3,}", b):
            raise SystemExit(f"latin at {i}: {b}")


def main() -> int:
    lines = [f"{i:03d}、{body}" for i, body in enumerate(BODIES, 1)]
    validate(lines)
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": lines}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    subprocess.run(cmd, cwd=str(ROOT), check=True)
    print("written/book_id/150/grok")
    print(f"written\t{BOOK_ID}")
    print("150")
    print("grok")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
