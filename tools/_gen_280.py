NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")

def main() -> None:
    assert len(HIGHLIGHTS) == 150, len(HIGHLIGHTS)
    assert len(set(HIGHLIGHTS)) == 150
    starts = Counter(h[:18] for h in HIGHLIGHTS)
    assert starts.most_common(1)[0][1] < 4, starts.most_common(5)
    fixed = []
    for h in HIGHLIGHTS:
        # traditional fix
        h = h.replace("等于", "等於")
        m = re.match(r"^([^：:]{1,12})[：:]", h)
        if m and not m.group(1).endswith(NATURAL):
            h = h.replace("：", "，", 1).replace(":", "，", 1)
        assert len(h) >= 12
        fixed.append(h)
    payload = {
        "id": BOOK_ID,
        "highlights": [f"{i:03d}、{h}" for i, h in enumerate(fixed, 1)],
    }
    path = Path(__file__).with_name(f".findbook_results_grok_{BOOK_ID}.json")
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print("wrote", path, len(payload["highlights"]))


if __name__ == "__main__":
    main()
