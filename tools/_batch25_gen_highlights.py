"""Generate grok highlight result JSON for batch25 books."""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def pack(book_id: str, bodies: list[str]) -> Path:
    """Write results JSON for one book."""
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} expected 150 got {len(bodies)}")
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise SystemExit(f"{book_id} line {i} too short: {body}")
    if len(set(bodies)) != 150:
        raise SystemExit(f"{book_id} has duplicate bodies")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]
    path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"wrote {path.name}")
    return path


