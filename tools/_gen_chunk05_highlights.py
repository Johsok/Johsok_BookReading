def main() -> None:
    results_dir = ROOT / "tools"
    for book_id, title, author, bodies in BOOKS:
        highlights = pack(bodies)
        validate_highlights(book_id, highlights, title, author)
        out = results_dir / f".findbook_results_grok_{book_id}.json"
        payload = {"id": book_id, "highlights": highlights}
        out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"wrote\t{out.name}\t{len(highlights)}")
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            "02_psychology_growth",
            "--results",
            str(out),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        print(proc.stdout, end="")
        if proc.returncode != 0:
            print(proc.stderr, file=sys.stderr)
            raise SystemExit(proc.returncode)
        book_path = ROOT / "Books" / "02_psychology_growth" / f"{book_id}.json"
        saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
        status = saved.get("chatgptStatus")
        n = len(saved.get("chatgptHighlights") or [])
        print(f"confirm\t{book_id}\t{status}\t{n}")
        if status != "complete" or n != 150:
            raise SystemExit(f"confirm failed for {book_id}")


if __name__ == "__main__":
    main()
