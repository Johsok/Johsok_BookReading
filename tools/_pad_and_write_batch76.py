def pad(name: str, lines: list[str]) -> list[str]:
    out = list(lines)
    # drop leftover replacement chars if any
    out = [x.replace("\ufffd", "") for x in out]
    out = [x for x in out if len(x) >= 12]
    need = 150 - len(out)
    if need < 0:
        raise SystemExit(f"{name} has {len(out)} > 150")
    extra = EXTRA[name]
    if len(extra) < need:
        raise SystemExit(f"{name} extra {len(extra)} < need {need}")
    # keep uniqueness
    seen = set(out)
    for body in extra:
        if body in seen:
            continue
        out.append(body)
        seen.add(body)
        if len(out) == 150:
            break
    if len(out) != 150:
        raise SystemExit(f"{name} ended with {len(out)}")
    return out


