def write_and_complete(book_id: str, category_id: str) -> None:
    lines = BOOKS[book_id]
    payload = {"id": book_id, "highlights": numbered(lines)}
    results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    results_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        category_id,
        "--results",
        str(results_path),
    ]
    print("RUN", book_id)
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout or "")
    sys.stderr.write(proc.stderr or "")
    if proc.returncode != 0:
        raise SystemExit(f"FAIL {book_id} code={proc.returncode}")


