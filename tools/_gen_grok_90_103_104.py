def validate_bodies(book_id: str, bodies: list[str], title: str = "", author: str = "") -> None:
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise SystemExit(f"{book_id} duplicates")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"{book_id} repeated starts: {starts.most_common(5)}")
    short_colon = []
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise SystemExit(f"{book_id} #{i} too short")
        for p in FORBIDDEN:
            if p in body:
                raise SystemExit(f"{book_id} #{i} forbidden {p}")
        if "｜" in body or "\n" in body:
            raise SystemExit(f"{book_id} #{i} bad format")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"{book_id} #{i} step wording")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            short_colon.append(i)
        # simplified char quick check
        for ch in "国发会总后对着时样体现":
            if ch in body:
                raise SystemExit(f"{book_id} #{i} simplified? {ch}")
    if len(short_colon) >= 3:
        raise SystemExit(f"{book_id} short colon lines: {short_colon}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise SystemExit(f"{book_id} repeats {label}")


def write_and_complete(book_id: str, bodies: list[str]) -> None:
    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    book = json.loads(book_path.read_text(encoding="utf-8-sig"))
    validate_bodies(book_id, bodies, str(book.get("title", "")), str(book.get("author", "")))
    # fix known simplified slips before write
    fixed = []
    for b in bodies:
        b = b.replace("magnify", "放大")
        b = b.replace("活不过", "活不過")
        b = b.replace("无聊", "無聊")
        b = b.replace("偏执", "偏執")
        b = b.replace("余度", "餘度")
        fixed.append(b)
    validate_bodies(book_id, fixed, str(book.get("title", "")), str(book.get("author", "")))
    payload = {
        "id": book_id,
        "highlights": [f"{i:03d}、{b}" for i, b in enumerate(fixed, 1)],
    }
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    proc = subprocess.run(
        [
            sys.executable,
            str(TOOLS / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ],
        cwd=str(ROOT),
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    sys.stdout.write(proc.stdout or "")
    if proc.returncode != 0:
        sys.stderr.write(proc.stderr or "")
        raise SystemExit(f"{book_id} complete failed: {proc.returncode}")
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    print(
        f"ok\t{book_id}\t{len(saved['chatgptHighlights'])}\t{saved.get('highlightsSource')}"
    )


