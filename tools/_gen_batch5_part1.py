from __future__ import annotations

import json
import re
import subprocess
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL_COLON_SUFFIXES = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN = ("本書", "作者指出", "本章", "這一章")


def validate(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id}: count={len(highlights)}")
    short_colon: list[int] = []
    bodies: list[str] = []
    for i, line in enumerate(highlights, 1):
        exp = f"{i:03d}、"
        if not isinstance(line, str) or not line.startswith(exp):
            raise ValueError(f"{book_id} #{i} bad number")
        if "\n" in line or "\r" in line or "｜" in line:
            raise ValueError(f"{book_id} #{i} bad format")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short: {body}")
        if any(p in body for p in FORBIDDEN):
            raise ValueError(f"{book_id} #{i} forbidden")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(i)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short colon: {short_colon[:10]}")
    if len(set(bodies)) != len(bodies):
        c = Counter(bodies)
        dups = [b for b, n in c.items() if n > 1][:3]
        raise ValueError(f"{book_id} duplicate: {dups}")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    worst = starts.most_common(1)[0]
    if worst[1] >= 4:
        raise ValueError(f"{book_id} repeated start {worst}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")


def pack(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"need 150 got {len(bodies)}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, category_id: str, bodies: list[str], title: str, author: str) -> None:
    highlights = pack(bodies)
    validate(book_id, highlights, title, author)
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        category_id,
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout)
    if proc.returncode != 0:
        print(proc.stderr, file=sys.stderr)
        raise SystemExit(proc.returncode)
    print(f"OK {book_id}")


