def write_book(book_id: str, title: str, author: str, bodies: list[str]) -> None:
    """Validate 150 highlights, write results JSON, caller runs complete."""
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} expected 150, got {len(bodies)}")
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    validate_highlights(book_id, highlights, title, author)
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"results\t{out}")


