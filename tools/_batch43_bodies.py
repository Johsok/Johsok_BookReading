def _check(name: str, bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"{name} len={len(bodies)}")
    if len(set(bodies)) != 150:
        raise ValueError(f"{name} has duplicates")
    for b in bodies:
        if len(b) < 12:
            raise ValueError(f"{name} short: {b}")
    return bodies


