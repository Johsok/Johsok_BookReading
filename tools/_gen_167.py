NUMBER_RE = re.compile(r"^\d{3}、")
NAT = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")


def validate(highlights, title="", author=""):
    if len(highlights) != 150:
        raise ValueError(f"count {len(highlights)}")
    short, bodies = [], []
    for i, line in enumerate(highlights, 1):
        exp = f"{i:03d}、"
        if not line.startswith(exp):
            raise ValueError(f"num {i}")
        if "\n" in line or "\r" in line or "｜" in line:
            raise ValueError(f"fmt {i}")
        body = NUMBER_RE.sub("", line, 1).strip()
        if len(body) < 12:
            raise ValueError(f"short {i}")
        for p in ("本書", "作者指出", "本章", "這一章"):
            if p in body:
                raise ValueError(f"forbid {i} {p}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"step {i}")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NAT):
            short.append(i)
        bodies.append(body)
    if len(short) >= 3:
        raise ValueError(f"colon {short}")
    if len(set(bodies)) != len(bodies):
        raise ValueError("dup")
    c = Counter(b[:18] for b in bodies if len(b) >= 18)
    if c.most_common(1)[0][1] >= 4:
        raise ValueError(f"starts {c.most_common(5)}")
    for label, val in (("title", title), ("author", author)):
        if val.strip() and sum(val in b for b in bodies) >= 2:
            raise ValueError(label)
    return True


