def pad_bodies(base: str, extra: str, n: int = 150) -> list[str]:
    """Combine base and extra bodies, ensuring exactly n unique lines."""
    bodies = parse_bodies(base)
    if len(bodies) < n:
        for line in parse_bodies(extra):
            if line not in bodies:
                bodies.append(line)
            if len(bodies) >= n:
                break
    if len(bodies) != n:
        raise ValueError(f"padding failed: got {len(bodies)} want {n}")
    if len(set(bodies)) != n:
        raise ValueError("duplicate bodies after padding")
    return bodies[:n]


