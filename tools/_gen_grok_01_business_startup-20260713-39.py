def main() -> None:
    assert len(highlights_body) == 150, len(highlights_body)

    number_re = re.compile(r"^\d{3}、")
    natural_colon_suffixes = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    forbidden_prefixes = ("本書", "作者指出", "本章", "這一章")
    title = "與奧客過招!破解難纏顧客的心理與應對術：企業與員工都該懂的顧客行為心理學"
    author = "溫亞凡"

    highlights = [f"{i:03d}、{body}" for i, body in enumerate(highlights_body, 1)]
    short_colon_lines = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise SystemExit(f"編號錯誤 {index}")
        if "\n" in line or "\r" in line or "｜" in line:
            raise SystemExit(f"禁用格式 {index}")
        body = number_re.sub("", line, count=1).strip()
        if len(body) < 12:
            raise SystemExit(f"過短 {index}: {body}")
        if any(prefix in body for prefix in forbidden_prefixes):
            raise SystemExit(f"禁用前綴 {index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"步驟贅詞 {index}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(natural_colon_suffixes):
            short_colon_lines.append(index)
        if any(ch in body for ch in "*`#"):
            raise SystemExit(f"疑似 Markdown {index}")
        bodies.append(body)

    if len(short_colon_lines) >= 3:
        raise SystemExit(f"短標籤冒號過多: {short_colon_lines}")
    if len(set(bodies)) != len(bodies):
        raise SystemExit("完全重複")
    repeated_starts = Counter(body[:18] for body in bodies if len(body) >= 18)
    if repeated_starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"重複開頭: {repeated_starts.most_common(5)}")
    if sum(title in body for body in bodies) >= 2:
        raise SystemExit("書名重複出現")
    if sum(author in body for body in bodies) >= 2:
        raise SystemExit("作者重複出現")

    out = Path(__file__).resolve().parent / ".findbook_results_grok_01_business_startup-20260713-39.json"
    payload = {"id": "01_business_startup-20260713-39", "highlights": highlights}
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {out}")
    print(f"count={len(highlights)} min_len={min(len(b) for b in bodies)}")
    print(f"max_start_dup={repeated_starts.most_common(3)}")


if __name__ == "__main__":
    main()
