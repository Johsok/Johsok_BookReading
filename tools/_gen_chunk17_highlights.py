# -*- coding: utf-8 -*-
"""Generate 150 highlights for chunk_17 psychology books and complete via writer."""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "tools"))
from findbook_writer import validate_highlights  # noqa: E402

CAT = "02_psychology_growth"


def numbered(bodies: list[str]) -> list[str]:
    return [f"{i:03d}、{b.strip()}" for i, b in enumerate(bodies, 1)]


def save_and_complete(book_id: str, bodies: list[str]) -> str:
    if len(bodies) != 150:
        raise SystemExit(f"{book_id}: got {len(bodies)}, need 150")
    for i, b in enumerate(bodies):
        if len(b.strip()) < 12:
            raise SystemExit(f"{book_id} line {i+1} too short: {b!r}")
    highlights = numbered(bodies)
    book_path = ROOT / "Books" / CAT / f"{book_id}.json"
    book = json.loads(book_path.read_text(encoding="utf-8-sig"))
    validate_highlights(book_id, highlights, book.get("title", ""), book.get("author", ""))
    out_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out_path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        CAT,
        "--results",
        str(out_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    text = (proc.stdout or "") + (proc.stderr or "")
    if proc.returncode != 0:
        raise SystemExit(f"FAIL {book_id}:\n{text}")
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    n = len(saved.get("chatgptHighlights") or [])
    status = saved.get("chatgptStatus")
    return f"{book_id}\tstatus={status}\thighlights={n}\n{text.strip()}"


