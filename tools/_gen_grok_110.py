def main() -> None:
    assert len(POINTS) == 150, len(POINTS)
    assert len(set(POINTS)) == 150, len(POINTS) - len(set(POINTS))
    starts = Counter(p[:18] for p in POINTS)
    assert starts.most_common(1)[0][1] < 4, starts.most_common(5)
    issues = []
    for i, p in enumerate(POINTS, 1):
        if any(x in p for x in ("本書", "作者指出", "本章", "這一章", "｜")):
            issues.append((i, p))
        if len(p) < 12:
            issues.append((i, "short", p))
        m = re.match(r"^([^：:]{1,12})[：:]", p)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            issues.append((i, "colon", p))
    assert not issues, issues[:15]
    highlights = [f"{i:03d}、{p}" for i, p in enumerate(POINTS, 1)]
    out = {"id": BOOK_ID, "highlights": highlights}
    path = Path(__file__).resolve().parent / f".findbook_results_grok_{BOOK_ID}.json"
    path.write_text(json.dumps(out, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {path} count={len(highlights)}")


if __name__ == "__main__":
    main()
