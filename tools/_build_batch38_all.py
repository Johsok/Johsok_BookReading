def pad150(lines: list[str], extras: list[str]) -> list[str]:
    out = [x.strip() for x in lines if x.strip()]
    i = 0
    while len(out) < 150:
        if i >= len(extras):
            raise SystemExit(f"need more extras, have {len(out)}")
        cand = extras[i].strip()
        i += 1
        if cand and cand not in out and len(cand) >= 12:
            out.append(cand)
    if len(out) > 150:
        out = out[:150]
    if len(out) != 150:
        raise SystemExit(len(out))
    if len(set(out)) != 150:
        # drop dupes and refill
        seen = []
        for x in out:
            if x not in seen:
                seen.append(x)
        j = 0
        while len(seen) < 150:
            cand = f"補充觀察{j+1:03d}，操作時記錄當日濕度與成品切面差異以便對照改善"
            # better use extras remaining
            if i < len(extras):
                cand = extras[i].strip()
                i += 1
            else:
                raise SystemExit("dupes cannot refill")
            if cand and cand not in seen and len(cand) >= 12:
                seen.append(cand)
            j += 1
        out = seen
    return out


def numbered(bodies: list[str]) -> list[str]:
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, category_id: str, bodies: list[str]) -> None:
    bodies = pad150(bodies, [])
    assert len(bodies) == 150
    for b in bodies:
        assert len(b) >= 12, b
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": numbered(bodies)}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        category_id,
        "--results",
        str(path),
    ]
    print("RUN", book_id)
    subprocess.run(cmd, cwd=str(ROOT), check=True)


