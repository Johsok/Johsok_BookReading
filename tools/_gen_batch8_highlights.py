    for book_id, category_id, lines in books:
        highlights = numbered(lines)
        results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        payload = {"id": book_id, "highlights": highlights}
        results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            category_id,
            "--results",
            str(results_path),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        if proc.returncode == 0:
            ok.append(book_id)
            print(f"OK {book_id}")
            if proc.stdout:
                print(proc.stdout)
        else:
            err = (proc.stderr or proc.stdout or "").strip()
            fail.append((book_id, err))
            print(f"FAIL {book_id}: {err}")
    print("---")
    print("success:", ok)
    print("failures:", fail)
    return 1 if fail else 0


