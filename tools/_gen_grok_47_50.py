def fix_typos(bodies: list[str]) -> list[str]:
    """Fix known typos introduced while drafting."""
    repl = {
        " ded 要問": "要問",
        "回檔买點": "回檔買點",
        "为前提": "為前提",
        "取决于": "取決於",
        "空頭里": "空頭裡",
        "会放棄": "會放棄",
        "被看见表": "被看見",
        "倾倒資訊": "傾倒資訊",
        "系统调整": "系統調整",
        "复述": "複述",
        "复盤": "複盤",
        "反复": "反覆",
        "卖不动": "賣不動",
    }
    out = []
    for b in bodies:
        for a, c in repl.items():
            b = b.replace(a, c)
        out.append(b)
    return out


