"""Generate grok highlight result JSON for _batch4.json books."""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = Path(__file__).resolve().parent


def numbered(lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise SystemExit(f"expected 150, got {len(lines)}")
    return [f"{i:03d}、{text}" for i, text in enumerate(lines, 1)]


def write_result(book_id: str, lines: list[str]) -> Path:
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": numbered(lines)}
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    return path


