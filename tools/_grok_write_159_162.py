"""Generate 150 Traditional Chinese highlights for books 159-162 and complete each."""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

ROOT = Path(r"C:\Users\johso\OneDrive\Desktop\Johsok_BookReading")
TOOLS = ROOT / "tools"
WRITER = TOOLS / "findbook_writer.py"


def numbered(lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise SystemExit(f"need 150, got {len(lines)}")
    out = []
    for i, body in enumerate(lines, 1):
        body = body.strip()
        if body.startswith(f"{i:03d}、"):
            out.append(body)
        else:
            out.append(f"{i:03d}、{body}")
    return out


def write_and_complete(book_id: str, lines: list[str]) -> None:
    highlights = numbered(lines)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(WRITER),
        "--root",
        str(ROOT),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout)
    sys.stderr.write(proc.stderr)
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    print(f"written/{book_id}/150/grok")


