ROOT = Path(__file__).resolve().parents[1]
TOOLS = Path(__file__).resolve().parent
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN = ("本書", "作者指出", "本章", "這一章")


def validate(highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"need 150, got {len(highlights)}")
    bodies: list[str] = []
    short: list[int] = []
    for i, line in enumerate(highlights, 1):
        exp = f"{i:03d}、"
        if not line.startswith(exp):
            raise ValueError(f"bad number {i}: {line[:40]}")
        if "｜" in line or "\n" in line or "\r" in line:
            raise ValueError(f"forbidden format {i}")
        body = line[len(exp) :].strip()
        if len(body) < 12:
            raise ValueError(f"too short {i}")
        if any(p in body for p in FORBIDDEN):
            raise ValueError(f"forbidden prefix {i}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"step wording {i}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL):
            short.append(i)
        bodies.append(body)
    if len(short) >= 3:
        raise ValueError(f"short colon {short}")
    if len(set(bodies)) != 150:
        raise ValueError("duplicate bodies")
    repeated = Counter(b[:18] for b in bodies if len(b) >= 18)
    if repeated and repeated.most_common(1)[0][1] >= 4:
        raise ValueError(f"repeated starts {repeated.most_common(5)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"repeated {label}")


def pack(texts: list[str]) -> list[str]:
    if len(texts) != 150:
        raise ValueError(len(texts))
    return [f"{i:03d}、{t}" for i, t in enumerate(texts, 1)]


def write_and_complete(book_id: str, texts: list[str], title: str, author: str) -> None:
    highlights = pack(texts)
    validate(highlights, title, author)
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(path),
    ]
    result = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, encoding="utf-8")
    print(result.stdout)
    if result.returncode != 0:
        print(result.stderr)
        raise SystemExit(result.returncode)
    print(f"OK {book_id} {len(highlights)}")


