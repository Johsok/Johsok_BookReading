def main() -> None:
    bodies = []
    for b in BODIES:
        b = (
            b.replace(" alone", "")
            .replace("延迟", "延遲")
            .replace("瞻妄", "譫妄")
            .strip()
        )
        # fix awkward leftover from removing alone
        b = b.replace("而非時數。", "而非只有使用時數。").replace("而非時數", "而非只有使用時數")
        bodies.append(b)
    if len(bodies) != 150:
        raise SystemExit(f"count={len(bodies)}")
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    validate_highlights(BOOK_ID, highlights, TITLE, AUTHOR)
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"wrote {out} count={len(highlights)}")


if __name__ == "__main__":
    main()
