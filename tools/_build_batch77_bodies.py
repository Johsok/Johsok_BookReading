def fix_tw(s: str) -> str:
    for a, b in SIMPLIFIED_FIX.items():
        s = s.replace(a, b)
    return s


def load_extract(name: str) -> list[str]:
    path = ROOT / f"_extract_{name}.txt"
    if not path.exists():
        return []
    lines = []
    for raw in path.read_text(encoding="utf-8").splitlines():
        s = fix_tw(raw.strip())
        if len(s) >= 12:
            lines.append(s)
    return lines


