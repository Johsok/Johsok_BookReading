def validate_bodies(book_id: str, bodies: list[str], title: str = "", author: str = "") -> None:
    if len(bodies) != 150:
        raise ValueError(f"{book_id} need 150, got {len(bodies)}")
    short_colon = []
    forbidden = ("本書", "作者指出", "本章", "這一章")
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise ValueError(f"{book_id}#{i} short: {body}")
        if "｜" in body or "\n" in body:
            raise ValueError(f"{book_id}#{i} bad char")
        for p in forbidden:
            if p in body:
                raise ValueError(f"{book_id}#{i} forbidden {p}")
        # fix leftover simplified / typos that fail quality
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id}#{i} step junk")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            short_colon.append(i)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short-colon {short_colon[:10]}")
    if len(set(bodies)) != 150:
        raise ValueError(f"{book_id} duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts {starts.most_common(5)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")


def pad_unique(base: list[str], extra: list[str], need: int = 150) -> list[str]:
    out = []
    seen = set()
    for s in base + extra:
        s = fix_tw(s.strip())
        # additional manual fixes
        s = s.replace("焦虑", "焦慮").replace("牵动", "牽動").replace("烟雾", "煙霧")
        s = s.replace("热量", "熱量").replace("脑内", "腦內").replace("优先", "優先")
        s = s.replace("园艺", "園藝").replace("换衣服", "換衣服").replace("果酱", "果醬")
        s = s.replace("贫血", "貧血").replace("vis 偏方", "吞食偏方")
        if "當多個小訊號同時出現" in s:
            s = "當多個小訊號同時出現，系統性評估勝過逐一吞食偏方"
        if len(s) < 12 or s in seen:
            continue
        seen.add(s)
        out.append(s)
        if len(out) >= need:
            break
    if len(out) < need:
        raise ValueError(f"not enough lines: {len(out)}")
    return out[:need]


