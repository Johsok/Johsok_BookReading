from __future__ import annotations

import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = Path(__file__).resolve().parent


def check(book_id: str, bodies: list[str], title: str, author: str) -> None:
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise SystemExit(f"{book_id} duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"{book_id} repeated starts: {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise SystemExit(f"{book_id} repeated {label}")
    for i, b in enumerate(bodies, 1):
        if len(b) < 12:
            raise SystemExit(f"{book_id} #{i} too short")
        if "｜" in b or any(x in b for x in ("本書", "作者指出", "本章", "這一章")):
            raise SystemExit(f"{book_id} #{i} forbidden")


def write_and_complete(book_id: str, bodies: list[str], title: str, author: str) -> None:
    check(book_id, bodies, title, author)
    payload = {
        "id": book_id,
        "highlights": [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)],
    }
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    result = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, encoding="utf-8")
    print(result.stdout, end="")
    print(result.stderr, end="", file=sys.stderr)
    if result.returncode != 0 or f"written\t{book_id}" not in result.stdout:
        raise SystemExit(f"{book_id} writer failed rc={result.returncode}")


