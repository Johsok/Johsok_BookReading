def extract_list(name: str, text: str) -> list[str]:
    m = re.search(rf"^{name} = \[(.*?)^\]\n", text, flags=re.M | re.S)
    if not m:
        raise RuntimeError(f"cannot find {name}")
    body = m.group(1)
    return re.findall(r'^\s*"(.*)"\s*,?\s*$', body, flags=re.M)


def replace_list(name: str, text: str, items: list[str]) -> str:
    block = name + " = [\n" + "".join(f'    "{it}",\n' for it in items) + "]\n"
    new_text, n = re.subn(
        rf"^{name} = \[.*?^\]\n",
        block,
        text,
        count=1,
        flags=re.M | re.S,
    )
    if n != 1:
        raise RuntimeError(f"replace failed for {name}: {n}")
    return new_text


