def insert_extras(src: str, var: str, lines: list[str]) -> str:
    match = re.search(rf"({var}\s*=\s*\[)", src)
    if not match:
        raise SystemExit(f"missing {var}")
    start = match.end()
    rest = src[start:]
    end_rel = re.search(r"\n\]\n", rest)
    if not end_rel:
        raise SystemExit(f"no end {var}")
    end = start + end_rel.start()
    body = src[start:end].rstrip()
    if not body.endswith(","):
        body = body + ","
    body = body + "\n"
    add = "".join(f'    "{line}",\n' for line in lines)
    return src[:start] + body + add + src[end:]


