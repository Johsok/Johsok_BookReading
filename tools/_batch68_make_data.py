def pad_unique(seed: list[str], fillers: list[str], n: int = 150) -> list[str]:
    seen = set()
    out = []
    for s in seed + fillers:
        s = s.strip()
        if len(s) < 12 or s in seen:
            continue
        seen.add(s)
        out.append(s)
        if len(out) == n:
            return out
    raise RuntimeError(f"only {len(out)} lines, need {n}")


def write(name: str, lines: list[str]) -> None:
    assert len(lines) == 150, (name, len(lines))
    assert len(set(lines)) == 150, name
    (OUT / f"{name}.txt").write_text("\n".join(lines) + "\n", encoding="utf-8")
    print("wrote", name, len(lines))


