def load_bodies_from_source(path: Path) -> list[str]:
    text = path.read_text(encoding="utf-8")
    # Fix common syntax issues for b34
    text = text.replace('。"",', '。",')
    text = text.replace('。",",', '。",')
    m = re.search(r"BODIES = \[(.*?)\n\]", text, re.S)
    if not m:
        raise RuntimeError(f"no BODIES in {path}")
    block = m.group(1)
    bodies = re.findall(r'"([^"\n]+)"', block)
    return bodies


def uniq_extend(base: list[str], extras: list[str], need: int = 150) -> list[str]:
    out: list[str] = []
    seen: set[str] = set()
    for b in base + extras:
        b = b.strip()
        b = b.replace("听進", "聽進").replace("说明书", "說明書").replace("冷战", "冷戰")
        b = b.replace("空虚", "空虛").replace("支撑", "支撐").replace("突击", "突擊")
        b = b.replace("允许慢", "允許慢").replace("反复", "反覆").replace("口号", "口號")
        b = b.replace("学派", "學派").replace("书桌", "書桌")
        b = b.replace("棱角", "稜角")
        if "thrift" in b:
            b = b.replace("來自 thrift 與享樂", "來自節儉與享樂")
        if "ded" in b:
            b = "把時間視為不可再生資源，拖延等於對使命的慢性放棄。"
        if len(b) < 12:
            continue
        if b in seen:
            continue
        seen.add(b)
        out.append(b)
        if len(out) >= need:
            break
    # pad with numbered unique variants if still short
    i = 1
    while len(out) < need:
        candidate = f"把今日可執行的志業動作完成第{i}件，並記錄結果好讓進步可見。"
        # make unique per book by mixing
        candidate = f"以可檢查的小行動累積第{i:03d}次正向證據，讓改變不只停留在想法。"
        if candidate not in seen and len(candidate) >= 12:
            seen.add(candidate)
            out.append(candidate)
        i += 1
        if i > 500:
            raise RuntimeError("cannot pad")
    return out[:need]


def emit(book_id: str, bodies: list[str]) -> None:
    assert len(bodies) == 150
    assert len(set(bodies)) == 150
    for idx, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise RuntimeError(f"{book_id} short line {idx}")
        for bad in ("本書", "作者指出", "本章", "這一章"):
            if bad in body:
                raise RuntimeError(f"{book_id} forbidden {idx}")
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    out = ROOT / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {out.name} count={len(highlights)}")


