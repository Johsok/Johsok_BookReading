def main() -> int:
    success = []
    failures = []
    for book_id, category_id, lines in BOOKS:
        print(f"=== {book_id} count={len(lines)} ===")
        if len(lines) != 150:
            failures.append((book_id, f"count {len(lines)} != 150"))
            continue
        highlights = numbered(lines)
        results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        payload = {"id": book_id, "highlights": highlights}
        results_path.write_text(
            json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            category_id,
            "--results",
            str(results_path),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
        out = (proc.stdout or "") + (proc.stderr or "")
        print(out)
        if proc.returncode == 0 and "written" in out:
            success.append(book_id)
        else:
            failures.append((book_id, out.strip() or f"exit {proc.returncode}"))
    print("SUCCESS:", success)
    print("FAILURES:", failures)
    return 0 if not failures else 1


if __name__ == "__main__":
    raise SystemExit(main())
