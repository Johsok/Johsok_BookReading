def fix_english_leaks(bodies: list[str]) -> list[str]:
    """Remove accidental English words that slipped into highlights."""
    repl = {
        " Negotiability 會上升": "協商空間會上升",
        "归属感": "歸屬感",
        " consum ": "消耗",
        " alone ": "單獨",
        "一档": "一檔",
        "吓人": "嚇人",
    }
    out = []
    for b in bodies:
        for a, c in repl.items():
            b = b.replace(a, c)
        # fix specific broken lines
        if "Negotiability" in b:
            b = b.replace("Negotiability", "協商空間")
        if "consum" in b:
            b = "成癮消耗的不只是藥，還有時間、信任與未來選項。"
        if "alone" in b and "懲罰" in b:
            b = "司法轉向治療的政策，承認單靠懲罰無法修補心理缺口。"
        out.append(b)
    return out


def main() -> None:
    results_summary = []
    for book_id, title, author, bodies in BOOKS:
        bodies = fix_english_leaks(bodies)
        # ensure 150
        if len(bodies) != 150:
            raise SystemExit(f"{book_id} count={len(bodies)}")
        highlights = pack(bodies)
        validate_highlights(book_id, highlights, title, author)
        out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        out.write_text(
            json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            CATEGORY,
            "--results",
            str(out),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        print(f"=== {book_id} ===")
        print(proc.stdout)
        if proc.returncode != 0:
            print(proc.stderr)
            raise SystemExit(proc.returncode)
        # verify
        book_path = ROOT / "Books" / CATEGORY / f"{book_id}.json"
        data = json.loads(book_path.read_text(encoding="utf-8"))
        n = len(data.get("chatgptHighlights", []))
        status_ok = n == 150
        results_summary.append((book_id, n, status_ok, data.get("title", "")[:30]))
        print(f"VERIFY highlights={n} ok={status_ok}")

    print("\nSUMMARY")
    for book_id, n, ok, t in results_summary:
        print(f"{book_id}: {n} complete={'YES' if ok else 'NO'} | {t}")


