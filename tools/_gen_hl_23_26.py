def write_and_complete(book_id: str, bodies: list[str]) -> None:
    title = ""
    author = ""
    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    if book_path.exists():
        meta = json.loads(book_path.read_text(encoding="utf-8-sig"))
        title = str(meta.get("title", ""))
        author = str(meta.get("author", ""))
    highlights = pack(bodies)
    validate_highlights(book_id, highlights, title, author)
    results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout or "")
    if proc.returncode != 0:
        sys.stderr.write(proc.stderr or "")
        raise SystemExit(proc.returncode)
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    src = saved.get("highlightsSource")
    n = len(saved.get("chatgptHighlights") or [])
    print(f"written/{book_id}/{n}/{src}")


if __name__ == "__main__":
    write_and_complete("01_business_startup-20260716-23", BOOK23)
