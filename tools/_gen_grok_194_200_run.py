def process(book_id: str, highlights: list[str]) -> None:
    spec = importlib.util.spec_from_file_location("fw", TOOLS / "findbook_writer.py")
    fw = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    spec.loader.exec_module(fw)
    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    book = json.loads(book_path.read_text(encoding="utf-8-sig"))
    cleaned = fw.validate_highlights(
        book_id, highlights, str(book.get("title", "")), str(book.get("author", ""))
    )
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": cleaned}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    print("RUN", book_id)
    subprocess.check_call(cmd, cwd=str(ROOT))


def main() -> None:
    for book_id, highlights in BOOKS.items():
        process(book_id, highlights)
    print("done", len(BOOKS))


if __name__ == "__main__":
    main()
