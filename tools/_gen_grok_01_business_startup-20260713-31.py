    if len(BODIES) != 150:
        raise SystemExit(f"need 150 bodies, got {len(BODIES)}")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(BODIES, 1)]
    book_id = "01_business_startup-20260713-31"
    title = "企業估值投資：華爾街頂尖智庫的估值心法，看透企業體質好壞，正確買進"
