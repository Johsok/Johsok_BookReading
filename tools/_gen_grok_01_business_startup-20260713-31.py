    validate_highlights(book_id, highlights, title, author)
    # forbid markdown-ish and book/author echoes
    for i, body in enumerate(BODIES, 1):
        if len(body) < 12:
            raise SystemExit(f"short body {i}: {body}")
        if any(x in body for x in ("｜", "|", "#", "*", "`", "本書", "作者指出", "本章", "這一章")):
            raise SystemExit(f"forbidden token in {i}")
        if title in body or author in body:
            raise SystemExit(f"title/author leak in {i}")
    OUT.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {OUT} count={len(highlights)}")
