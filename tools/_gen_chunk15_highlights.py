        ("02_psychology_growth-20260718-131", "完形諮商與心理治療技術", "Phil Joyce & Charlotte Sills 張莉莉", BOOK_131),
        ("02_psychology_growth-20260718-132", "有病的其實是我媽，卻要我去諮商：寫給青少年和家長的心理圖文書", "大衛・古席翁 穆佐 Geraldine LEE", BOOK_132),
