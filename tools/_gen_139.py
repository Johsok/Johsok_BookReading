def main() -> None:
    bodies_raw = list(RAW)
    # fix simplified / english
    repl = {
        "兑现": "兌現",
        "沉淀": "沉澱",
        "pricing": "反映在價格中",
        "Crowding": "擁擠",
    }
    bodies_raw = [s for s in bodies_raw]
    for i, s in enumerate(bodies_raw):
        for a, b in repl.items():
            s = s.replace(a, b)
        bodies_raw[i] = s
    while len(bodies_raw) < 150:
        bodies_raw.append(EXTRA[len(bodies_raw) - len(RAW)])
    assert len(bodies_raw) == 150, len(bodies_raw)

    highlights = [f"{i:03d}、{text}" for i, text in enumerate(bodies_raw, 1)]
    NUMBER_RE = re.compile(r"^\d{3}、")
    NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    forbidden = ("本書", "作者指出", "本章", "這一章")
    short = []
    bodies = []
    for i, line in enumerate(highlights, 1):
        body = NUMBER_RE.sub("", line, 1).strip()
        assert len(body) >= 12, (i, body)
        assert "｜" not in body
        assert not any(p in body for p in forbidden)
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            short.append(i)
        bodies.append(body)
    assert len(short) < 3, short
    assert len(set(bodies)) == 150
    starts = Counter(b[:18] for b in bodies)
    bad = [(k, v) for k, v in starts.items() if v >= 4]
    assert not bad, bad
    assert sum(TITLE in b for b in bodies) < 2
    assert sum(AUTHOR in b for b in bodies) < 2

    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print("wrote", out, "n=", len(highlights))
    r = subprocess.run(
        [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ],
        cwd=str(ROOT),
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    print(r.stdout)
    print(r.stderr)
    sys.exit(r.returncode)


if __name__ == "__main__":
    main()
