# -*- coding: utf-8 -*-
"""Generate findbook results JSON for batch19 books."""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def save(book_id: str, points: list[str]) -> Path:
    if len(points) != 150:
        raise SystemExit(f"{book_id}: expected 150, got {len(points)}")
    highlights = []
    for i, body in enumerate(points, 1):
        body = body.strip()
        if len(body) < 12:
            raise SystemExit(f"{book_id}: line {i} too short: {body}")
        highlights.append(f"{i:03d}、{body}")
    path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"wrote {path.name}")
    return path


