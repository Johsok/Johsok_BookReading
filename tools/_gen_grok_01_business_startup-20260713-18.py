def validate(highlights: list[str]) -> None:
    short_colon_lines = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"第 {index} 點編號錯誤")
        if "\n" in line or "\r" in line or "｜" in line or "|" in line or "*" in line or "#" in line:
            raise ValueError(f"第 {index} 點含禁用格式")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"第 {index} 點正文過短: {body}")
        if any(prefix in body for prefix in FORBIDDEN_PREFIXES):
            raise ValueError(f"第 {index} 點含禁用來源前綴")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"第 {index} 點含面向／步驟贅詞")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon_lines.append(index)
        if TITLE and TITLE in body:
            raise ValueError(f"第 {index} 點含書名")
        if AUTHOR and AUTHOR in body:
            raise ValueError(f"第 {index} 點含作者")
        bodies.append(body)
    if len(short_colon_lines) >= 3:
        raise ValueError(f"有 {len(short_colon_lines)} 點疑似短標籤加冒號")
    if len(set(bodies)) != len(bodies):
        dups = [b for b, c in Counter(bodies).items() if c > 1]
        raise ValueError(f"含完全重複重點: {dups[:3]}")
    repeated_starts = Counter(body[:18] for body in bodies if len(body) >= 18)
    bad = [(k, v) for k, v in repeated_starts.items() if v >= 4]
    if bad:
        raise ValueError(f"有大量重複固定開頭: {bad[:3]}")


def main() -> int:
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(BODIES, 1)]
    validate(highlights)
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    subprocess.run(cmd, cwd=str(ROOT), check=True)
    print(f"written/book_id/{BOOK_ID}/150/grok")
    return 0


