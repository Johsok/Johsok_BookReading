def pad(base: list[str], extra: list[str], n: int = 150) -> list[str]:
    out = list(base)
    i = 0
    while len(out) < n:
        out.append(extra[i % len(extra)])
        i += 1
        if i > 1000:
            raise RuntimeError("pad failed")
    # ensure uniqueness by lightly varying duplicates if any
    seen = set()
    unique = []
    for idx, body in enumerate(out):
        b = body
        suffix = 0
        while b in seen:
            suffix += 1
            b = f"{body.rstrip('。')}（補述{suffix}）。"
        seen.add(b)
        unique.append(b)
    return unique[:n]
