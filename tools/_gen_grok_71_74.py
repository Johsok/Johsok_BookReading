def complete_one(book_id: str, items: list[str]) -> None:
    highlights = fmt(items)
    validate_highlights(book_id, highlights, title="", author="")
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    if proc.returncode != 0:
        print(proc.stdout)
        print(proc.stderr)
        raise SystemExit(f"complete failed: {book_id}")
    line = [ln for ln in proc.stdout.splitlines() if ln.startswith("written")]
    print(line[0] if line else proc.stdout.strip())
    print(f"source=grok count=150 book_id={book_id}")


