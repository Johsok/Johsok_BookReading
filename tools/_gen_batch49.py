def main() -> None:
    ok = []
    failed = []
    for book in BOOKS:
        book_id = book["id"]
        bodies = BODY_MAP[book["bodies"]]
        try:
            assert len(bodies) == 150, (book_id, len(bodies))
            path = write_results(book_id, bodies, book["title"], book["author"])
            run_writer(book["categoryId"], path)
            ok.append(book_id)
            print("OK", book_id)
        except Exception as exc:  # noqa: BLE001
            failed.append((book_id, str(exc)))
            print("FAIL", book_id, exc)
    print("SUCCESS", ok)
    print("FAILED", failed)
    if failed:
        sys.exit(1)


if __name__ == "__main__":
    main()
