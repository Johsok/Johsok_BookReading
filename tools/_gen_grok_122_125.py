from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = ROOT / "tools"

NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL_COLON_SUFFIXES = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN_PREFIXES = ("本書", "作者指出", "本章", "這一章")


def validate_local(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id} count {len(highlights)}")
    short_colon_lines = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"{book_id} bad number {index}")
        if "\n" in line or "\r" in line or "｜" in line:
            raise ValueError(f"{book_id} forbidden format {index}")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} too short {index}: {body}")
        if any(prefix in body for prefix in FORBIDDEN_PREFIXES):
            raise ValueError(f"{book_id} forbidden prefix {index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} step wording {index}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon_lines.append(index)
        bodies.append(body)
    if len(short_colon_lines) >= 3:
        raise ValueError(f"{book_id} short colon {short_colon_lines}")
    if len(set(bodies)) != len(bodies):
        raise ValueError(f"{book_id} duplicates")
    repeated_starts = Counter(body[:18] for body in bodies if len(body) >= 18)
    if repeated_starts and repeated_starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts {repeated_starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        normalized = str(value).strip()
        if normalized and sum(normalized in body for body in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")


def pack(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"need 150 got {len(bodies)}")
    return [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]


def write_result(book_id: str, bodies: list[str], title: str, author: str) -> Path:
    highlights = pack(bodies)
    validate_local(book_id, highlights, title, author)
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    return path


