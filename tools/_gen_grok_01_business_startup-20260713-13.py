def main() -> int:
    if len(BODIES) != 150:
        raise SystemExit(f"need 150 bodies, got {len(BODIES)}")

    highlights: list[str] = []
    for index, body in enumerate(BODIES, 1):
        text = body.strip()
        if len(text) < 12:
            raise SystemExit(f"line {index} too short: {text}")
        if "｜" in text or "\n" in text or "\r" in text:
            raise SystemExit(f"line {index} has forbidden format")
        if any(prefix in text for prefix in FORBIDDEN_PREFIXES):
            raise SystemExit(f"line {index} has forbidden prefix")
        if re.search(r".{1,8}面第\d+步[，,]", text) or re.match(r"^第\d+步[，,]", text):
            raise SystemExit(f"line {index} has step wording")
        if TITLE in text or AUTHOR in text:
            raise SystemExit(f"line {index} repeats title/author")
        for name_part in ("溫伯格", "傑拉爾德", "顧問成功的祕密"):
            if name_part in text:
                raise SystemExit(f"line {index} contains forbidden name/title part")
        highlights.append(f"{index:03d}、{text}")

    if len(set(BODIES)) != 150:
        raise SystemExit("duplicate bodies")

    repeated_starts = Counter(body[:18] for body in BODIES if len(body) >= 18)
    top_start, top_count = repeated_starts.most_common(1)[0]
    if top_count >= 4:
        raise SystemExit(f"repeated start: {top_start} x{top_count}")

    short_colon_lines = []
    for index, body in enumerate(BODIES, 1):
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon_lines.append(index)
    if len(short_colon_lines) >= 3:
        raise SystemExit(f"short colon lines: {short_colon_lines}")

    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
        newline="\n",
    )
    print(f"wrote {out}")
    print(f"count={len(highlights)} top_start={top_start!r}x{top_count}")
    print(f"short_colon={short_colon_lines}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
