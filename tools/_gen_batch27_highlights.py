def ensure_150(lines: list[str], extra: list[str], book_id: str) -> list[str]:
    """Pad or trim to exactly 150 unique lines."""
    out = list(lines)
    seen = set(out)
    for item in extra:
        if len(out) >= 150:
            break
        if item not in seen:
            out.append(item)
            seen.add(item)
    if len(out) < 150:
        raise ValueError(f"{book_id} still short: {len(out)}")
    if len(out) > 150:
        out = out[:150]
    return out


