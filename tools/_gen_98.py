def validate(title: str, author: str, items: list[str]) -> list[str]:
    # normalize common simplified leftovers
    fixed = []
    for h in items:
        h = (
            h.replace("口号", "口號")
            .replace("信誉", "信譽")
            .replace("说明书", "說明書")
            .replace("撕破脸", "撕破臉")
            .replace("不等于", "不等於")
            .replace("伪造", "偽造")
            .replace("售后", "售後")
        )
        # avoid English orphan word issue - rewrite that line if needed
        h = h.replace(" orphan 產品", "被停產卻無零件的產品")
        fixed.append(h)
    items = fixed
    assert len(items) == 150, len(items)
    short = []
    bodies = []
    for i, body in enumerate(items, 1):
        assert len(body) >= 12, (i, body, len(body))
        assert "｜" not in body and "\n" not in body
        for bad in ("本書", "作者指出", "本章", "這一章"):
            assert bad not in body, (i, body)
        assert not re.search(r".{1,8}面第\d+步[，,]", body)
        assert not re.match(r"^第\d+步[，,]", body)
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL):
            short.append((i, body))
        bodies.append(body)
    assert len(short) < 3, short
    assert len(set(bodies)) == 150, len(set(bodies))
    top = Counter(b[:18] for b in bodies).most_common(1)[0]
    assert top[1] < 4, top
    for label, value in (("書名", title), ("作者", author)):
        # author may be compound; check each
        hits = sum(value in b for b in bodies)
        assert hits < 2, (label, value, hits)
    # also check individual authors
    for part in ("羅崑泉", "陳雅莉", "喬山"):
        hits = sum(part in b for b in bodies)
        # 喬山 is brand/theme - title contains 喬山; avoid >=2 if it's in title
        if part in title or part in author:
            assert hits < 2, (part, hits)
    return [f"{i:03d}、{h}" for i, h in enumerate(items, 1)], items


