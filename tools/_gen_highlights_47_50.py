def make_lines(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150 bodies, got {len(bodies)}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, bodies: list[str]) -> None:
    lines = make_lines(bodies)
    validate_highlights(book_id, lines)
    results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": lines, "source": "grok"}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    import subprocess

    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout.strip())
    if proc.returncode != 0:
        print(proc.stderr, file=sys.stderr)
        raise SystemExit(proc.returncode)
    # verify
    book = json.loads((ROOT / "Books" / "01_business_startup" / f"{book_id}.json").read_text(encoding="utf-8-sig"))
    src = book.get("highlightsSource")
    n = len(book.get("chatgptHighlights") or [])
    print(f"verify\t{book_id}\t{n}\t{src}\t{book.get('chatgptStatus')}")


