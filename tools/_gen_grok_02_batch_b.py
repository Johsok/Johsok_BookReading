def expand(theme_lines: list[str], fillers: list[str]) -> list[str]:
    """Ensure >=150 by appending unique filler lines."""
    out = list(theme_lines)
    seen = set(out)
    for f in fillers:
        if len(out) >= 160:
            break
        if f not in seen and len(f) >= 12:
            out.append(f)
            seen.add(f)
    return out


