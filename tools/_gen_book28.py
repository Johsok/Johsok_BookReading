def main() -> None:
    book_id = "01_business_startup-20260716-28"
    if len(BODIES) != 150:
        raise SystemExit(f"need 150, got {len(BODIES)}")
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(BODIES, 1)]
    validate_highlights(book_id, highlights)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    results_path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    proc = subprocess.run(
        [
            sys.executable,
            str(TOOLS / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(results_path),
        ],
        cwd=str(ROOT),
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    sys.stdout.write(proc.stdout)
    if proc.returncode != 0:
        sys.stderr.write(proc.stderr)
        raise SystemExit(proc.returncode)
    print(f"written/{book_id}/150/grok")


if __name__ == "__main__":
    main()
