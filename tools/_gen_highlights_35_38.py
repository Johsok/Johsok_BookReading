def validate_local(book_id: str, bodies: list[str], title: str = "", author: str = "") -> None:
    if len(bodies) != 150:
        raise ValueError(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        dup = [b for b in bodies if bodies.count(b) > 1][:3]
        raise ValueError(f"{book_id} duplicates: {dup}")
    short_colon = 0
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short: {body}")
        if "｜" in body or "\n" in body or "本書" in body or "作者指出" in body or "本章" in body:
            raise ValueError(f"{book_id} #{i} forbidden: {body}")
        if "面第" in body and "步" in body:
            raise ValueError(f"{book_id} #{i} step wording")
        import re

        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            short_colon += 1
    if short_colon >= 3:
        raise ValueError(f"{book_id} short colon lines={short_colon}")
    starts = Counter(b[:18] for b in bodies)
    if starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")


def write_and_complete(book_id: str, bodies: list[str], title: str = "", author: str = "") -> str:
    validate_local(book_id, bodies, title, author)
    payload = {
        "id": book_id,
        "highlights": [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)],
    }
    results = TOOLS / f".findbook_results_grok_{book_id}.json"
    results.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--root",
        str(ROOT),
        "--category-id",
        "01_business_startup",
        "--results",
        str(results),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout or "")
    if proc.returncode != 0:
        sys.stderr.write(proc.stderr or "")
        raise SystemExit(proc.returncode)
    book = json.loads(
        (ROOT / "Books" / "01_business_startup" / f"{book_id}.json").read_text(encoding="utf-8-sig")
    )
    src = book.get("highlightsSource", "")
    n = len(book.get("chatgptHighlights") or [])
    line = f"written/{book_id}/{n}/{src}"
    print(line)
    return line


