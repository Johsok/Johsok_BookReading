# -*- coding: utf-8 -*-
"""Generate 150 Traditional Chinese highlights for books 75-78 and complete."""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))
from tools.findbook_writer import validate_highlights  # noqa: E402


def numbered(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150 bodies, got {len(bodies)}")
    return [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]


def write_and_complete(book_id: str, title: str, author: str, bodies: list[str]) -> None:
    highlights = numbered(bodies)
    validate_highlights(book_id, highlights, title, author)
    results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--results",
        str(results_path),
        "--category-id",
        "01_business_startup",
    ]
    completed = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
    sys.stdout.write(completed.stdout)
    sys.stderr.write(completed.stderr)
    if completed.returncode != 0:
        raise SystemExit(f"complete failed for {book_id}: {completed.returncode}")
    print(f"written/{book_id}/150/grok")


