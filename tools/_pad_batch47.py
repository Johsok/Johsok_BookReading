def main() -> None:
    src = PATH.read_text(encoding="utf-8")
    for a, b in REPLACEMENTS:
        src = src.replace(a, b)

    # Remove asserts temporarily for exec
    exec_src = re.sub(r"^assert .*$", "", src, flags=re.M)
    ns: dict = {}
    exec(compile(exec_src, str(PATH), "exec"), ns)

    for key, pad in PADS.items():
        cur = list(ns[key])
        need = 150 - len(cur)
        if need < 0:
            raise SystemExit(f"{key} already {len(cur)}")
        if need > len(pad):
            raise SystemExit(f"{key} need {need} but pad {len(pad)}")
        ns[key] = cur + pad[:need]
        print(key, "->", len(ns[key]))

    # rewrite file
    out = ['# -*- coding: utf-8 -*-',
           '"""150 highlight bodies each for batch47 healthcare books 21-25."""',
           'from __future__ import annotations',
           '']
    for key in ["H21", "H22", "H23", "H24", "H25"]:
        out.append(f"{key} = [")
        for line in ns[key]:
            out.append(f"    {line!r},")
        out.append("]")
        out.append("")
    for key in ["H21", "H22", "H23", "H24", "H25"]:
        out.append(f"assert len({key}) == 150, len({key})")
    out.append("")
    PATH.write_text("\n".join(out), encoding="utf-8")
    print("rewrote", PATH)


