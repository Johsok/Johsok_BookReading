def _check(name: str, lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise SystemExit(f"{name} len={len(lines)}")
    if len(set(lines)) != 150:
        raise SystemExit(f"{name} has duplicates")
    for i, ln in enumerate(lines, 1):
        if len(ln) < 12:
            raise SystemExit(f"{name}#{i} too short: {ln}")
    return lines


