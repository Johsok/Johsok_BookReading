def validate_local(book_id: str, title: str, author: str, highlights: list[str]) -> None:
    from collections import Counter
    import re

    assert len(highlights) == 150, (book_id, len(highlights))
    short_colon = []
    bodies = []
    for i, line in enumerate(highlights, 1):
        assert line.startswith(f"{i:03d}、"), (book_id, i, line[:20])
        assert "｜" not in line and "\n" not in line
        body = line.split("、", 1)[1]
        assert len(body) >= 12, (book_id, i, body)
        for bad in ("本書", "作者指出", "本章", "這一章"):
            assert bad not in body, (book_id, i, bad)
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            short_colon.append(i)
        bodies.append(body)
    assert len(short_colon) < 3, (book_id, short_colon)
    assert len(set(bodies)) == len(bodies), book_id
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    top = starts.most_common(1)[0]
    assert top[1] < 4, (book_id, top)
    for label, value in (("title", title), ("author", author)):
        v = value.strip()
        if v:
            assert sum(v in b for b in bodies) < 2, (book_id, label)


