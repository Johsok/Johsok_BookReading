def write_results(book_id: str, highlights: list[str]) -> Path:
    path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    return path


def run_writer(category_id: str, results_path: Path) -> None:
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        category_id,
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    if proc.returncode != 0:
        raise RuntimeError(
            f"writer failed for {results_path.name}:\nSTDOUT:\n{proc.stdout}\nSTDERR:\n{proc.stderr}"
        )
    print(proc.stdout.strip() or f"OK {results_path.name}")


def main() -> int:
    ok = []
    failed = []
    for book_id, category_id, factory in BOOKS:
        try:
            highlights = factory()
            path = write_results(book_id, highlights)
            run_writer(category_id, path)
            ok.append(book_id)
        except Exception as exc:  # noqa: BLE001
            failed.append((book_id, str(exc)))
            print(f"FAIL {book_id}: {exc}", file=sys.stderr)
    print("SUCCESS:", json.dumps(ok, ensure_ascii=False))
    print("FAILED:", json.dumps(failed, ensure_ascii=False))
    return 0 if not failed else 1


if __name__ == "__main__":
    raise SystemExit(main())
