from __future__ import annotations

import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = ROOT / "tools"


def finalize(book_id: str, bodies: list[str]) -> None:
    """Validate, write results JSON, and run writer complete."""
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise SystemExit(f"{book_id} duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"{book_id} repeated starts: {starts.most_common(3)}")
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise SystemExit(f"{book_id} #{i} too short")
        if "｜" in body or "\n" in body:
            raise SystemExit(f"{book_id} #{i} forbidden char")
        for bad in ("本書", "作者指出", "本章", "這一章"):
            if bad in body:
                raise SystemExit(f"{book_id} #{i} forbidden prefix {bad}")
    rows = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": rows}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout)
    sys.stderr.write(proc.stderr)
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    print(f"OK\t{book_id}\t150\tgrok")


