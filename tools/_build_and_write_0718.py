def ensure_150(items: list[str], pad: list[str], name: str) -> list[str]:
    items = [to_trad(x.strip()) for x in items]
    seen = set()
    uniq = []
    for x in items:
        if x not in seen:
            seen.add(x)
            uniq.append(x)
    items = uniq
    if len(items) > 150:
        items = items[:150]
    i = 0
    while len(items) < 150:
        if i >= len(pad):
            raise SystemExit(f"{name} need more pad, have {len(items)}")
        cand = to_trad(pad[i].strip())
        i += 1
        if cand not in seen:
            seen.add(cand)
            items.append(cand)
    return items


