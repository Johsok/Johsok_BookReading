NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL_COLON_SUFFIXES = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN_PREFIXES = ("本書", "作者指出", "本章", "這一章")


def numbered(lines: list[str]) -> list[str]:
    assert len(lines) == 150, len(lines)
    out = []
    for i, body in enumerate(lines, 1):
        body = body.strip()
        assert len(body) >= 12, (i, len(body), body)
        assert "｜" not in body
        assert not any(p in body for p in FORBIDDEN_PREFIXES)
        out.append(f"{i:03d}、{body}")
    return out


def local_validate(book_id: str, highlights: list[str], title: str, author: str) -> None:
    short_colon = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"{book_id} bad number {index}")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} short {index}: {body}")
        if any(p in body for p in FORBIDDEN_PREFIXES):
            raise ValueError(f"{book_id} forbidden {index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} step junk {index}")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(index)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short colon {short_colon}")
    if len(set(bodies)) != len(bodies):
        raise ValueError(f"{book_id} duplicates")
    bad = [(k, v) for k, v in Counter(b[:18] for b in bodies if len(b) >= 18).items() if v >= 4]
    if bad:
        raise ValueError(f"{book_id} repeated starts {bad[:5]}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats full {label}")


