def main() -> int:
    results_summary = []
    for book in BOOKS:
        lines = book["lines"]
        if len(lines) != 150:
            raise SystemExit(f"{book['id']} has {len(lines)} lines")
        highlights = numbered(lines)
        local_validate(book["id"], highlights, book["title"], book["author"])
        out = ROOT / "tools" / f".findbook_results_grok_{book['id']}.json"
        payload = {"id": book["id"], "highlights": highlights}
        out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            CATEGORY,
            "--results",
            str(out),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        ok = proc.returncode == 0
        book_path = ROOT / "Books" / CATEGORY / f"{book['id']}.json"
        saved = json.loads(book_path.read_text(encoding="utf-8-sig")) if book_path.exists() else {}
        count = len(saved.get("chatgptHighlights") or [])
        status = saved.get("chatgptStatus")
        results_summary.append(
            {
                "id": book["id"],
                "write_rc": proc.returncode,
                "stdout": (proc.stdout or "").strip(),
                "stderr": (proc.stderr or "").strip(),
                "status": status,
                "count": count,
                "ok": ok and status == "complete" and count == 150,
            }
        )
        print(json.dumps(results_summary[-1], ensure_ascii=False))
        if not results_summary[-1]["ok"]:
            print(proc.stdout)
            print(proc.stderr, file=sys.stderr)
    all_ok = all(r["ok"] for r in results_summary)
    return 0 if all_ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
