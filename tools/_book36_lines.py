def zh_len(s: str) -> int:
    return len(re.sub(r"\s+", "", s))


def validate(lines: list[str]) -> None:
    assert len(lines) == 150, len(lines)
    assert len(set(lines)) == 150
    forbidden = ("本書", "作者指出", "本章", "這一章", "｜", "上谷夫婦", "燒杯君和他的化學實驗")
    natural = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    short_colon = []
    for i, body in enumerate(lines, 1):
        assert zh_len(body) >= 12, (i, body, zh_len(body))
        for f in forbidden:
            assert f not in body, (i, f, body)
        assert "從《" not in body and "在《" not in body and "為閱讀線索" not in body
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(natural):
            short_colon.append(i)
    assert len(short_colon) < 3, short_colon
    starts = Counter(b[:18] for b in lines)
    bad = [(k, v) for k, v in starts.items() if v >= 4]
    assert not bad, bad
    print("OK", len(lines))


