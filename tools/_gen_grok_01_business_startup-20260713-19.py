def validate(bodies: list[str]) -> None:
    if len(bodies) != 150:
        raise SystemExit(f"count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise SystemExit("duplicate bodies")
    short_colon = []
    for index, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise SystemExit(f"short:{index}")
        if any(prefix in body for prefix in FORBIDDEN_PREFIXES):
            raise SystemExit(f"forbidden:{index}")
        if "｜" in body or "\n" in body or "\r" in body:
            raise SystemExit(f"format:{index}")
        if TITLE in body or AUTHOR in body:
            raise SystemExit(f"title/author:{index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"step:{index}")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(index)
    if len(short_colon) >= 3:
        raise SystemExit(f"short_colon:{short_colon}")
    repeated = Counter(body[:18] for body in bodies if len(body) >= 18)
    if repeated.most_common(1)[0][1] >= 4:
        raise SystemExit(f"repeated_starts:{repeated.most_common(3)}")


def main() -> None:
    validate(BODIES)
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(BODIES, 1)]
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"wrote\t{out}\t{len(highlights)}")


if __name__ == "__main__":
    main()
