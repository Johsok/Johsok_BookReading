def normalize(body: str) -> str:
    repl = {
        "差点": "差點",
        "杠杆": "槓桿",
        "庄家": "莊家",
        "赔率": "賠率",
        "信息": "資訊",
        "走开": "走開",
        "晒单": "曬單",
    }
    for a, b in repl.items():
        body = body.replace(a, b)
    return body


def main() -> None:
    lines = [normalize(ln.strip()) for ln in RAW.strip().splitlines() if ln.strip()]
    if len(lines) != 150:
        raise SystemExit(f"expected 150, got {len(lines)}")
    final = []
    for body in lines:
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            body = re.sub(r"^([^：:]{1,12})[：:]", r"\1，", body, count=1)
        final.append(body)
    if len(set(final)) != 150:
        # show dupes
        from collections import Counter as C
        c = C(final)
        raise SystemExit(f"duplicates: {[k for k,v in c.items() if v>1][:3]}")
    starts = Counter(b[:18] for b in final if len(b) >= 18)
    if starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"repeated starts: {starts.most_common(3)}")
    for body in final:
        if len(body) < 12:
            raise SystemExit(f"too short: {body}")
        if "｜" in body or any(p in body for p in ("本書", "作者指出", "本章", "這一章")):
            raise SystemExit(f"forbidden: {body}")
        if "孫惟微" in body or "賭客信條" in body:
            raise SystemExit(f"title/author leak: {body}")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(final, 1)]
    path = Path(__file__).resolve().parent / f".findbook_results_grok_{BOOK_ID}.json"
    path.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"wrote {path} count={len(highlights)}")


if __name__ == "__main__":
    main()
