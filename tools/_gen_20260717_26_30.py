def write_and_complete(book_id: str, title: str, author: str, points: list[str]) -> str:
    highlights = pack(points)
    validate(book_id, highlights, title, author)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(WRITER),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    output = (proc.stdout or "") + (proc.stderr or "")
    if proc.returncode != 0 or f"written\t{book_id}" not in (proc.stdout or ""):
        raise RuntimeError(f"{book_id} writer failed:\n{output}")
    return f"{book_id}\tOK\t150\twritten"


def main() -> int:
    results = []
    for book_id, title, author, points in BOOKS:
        print(f"processing {book_id} count={len(points)}", flush=True)
        line = write_and_complete(book_id, title, author, points)
        print(line, flush=True)
        results.append(line)
    print("---")
    for line in results:
        print(line)
    return 0


