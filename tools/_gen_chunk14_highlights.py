NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL_COLON_SUFFIXES = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN_PREFIXES = ("本書", "作者指出", "本章", "這一章")


def validate_local(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id} count={len(highlights)}")
    short_colon = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"{book_id} bad num {index}")
        if "\n" in line or "\r" in line or "｜" in line:
            raise ValueError(f"{book_id} forbidden format {index}")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} short {index}: {body}")
        if any(p in body for p in FORBIDDEN_PREFIXES):
            raise ValueError(f"{book_id} forbidden prefix {index}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} step pattern {index}")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(index)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short colon {short_colon}")
    if len(set(bodies)) != len(bodies):
        raise ValueError(f"{book_id} duplicates")
    rep = Counter(b[:18] for b in bodies if len(b) >= 18)
    if rep and rep.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts {rep.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")


def pack(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"need 150 got {len(bodies)}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


