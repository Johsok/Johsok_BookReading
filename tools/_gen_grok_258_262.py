def numbered(bodies: list[str]) -> list[str]:
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, bodies: list[str], title: str, author: str) -> None:
    if len(bodies) != 150:
        raise SystemExit(f"{book_id} bodies={len(bodies)} != 150")
    highlights = numbered(bodies)
    validate_highlights(book_id, highlights, title, author)
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
    print(proc.stdout)
    if proc.returncode != 0:
        print(proc.stderr)
        raise SystemExit(f"complete failed: {book_id}")
    saved = json.loads((ROOT / "Books" / "01_business_startup" / f"{book_id}.json").read_text(encoding="utf-8-sig"))
    assert saved.get("highlightsSource") == "grok"
    assert saved.get("chatgptStatus") == "complete"
    assert len(saved.get("chatgptHighlights") or []) == 150
    print(f"OK\t{book_id}\t{len(saved['chatgptHighlights'])}")


def main() -> None:
    # Only book 258 in this first run; others appended after success
    for book_id, meta in BOOKS.items():
        write_and_complete(book_id, meta["bodies"], meta["title"], meta["author"])


if __name__ == "__main__":
    main()
