from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "tools"


def numbered(lines: list[str]) -> list[str]:
    assert len(lines) == 150, len(lines)
    out = []
    for i, body in enumerate(lines, 1):
        body = body.strip()
        assert len(body) >= 12, (i, len(body), body)
        out.append(f"{i:03d}、{body}")
    return out


def write_result(book_id: str, lines: list[str]) -> Path:
    path = OUT / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": numbered(lines)}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    return path


