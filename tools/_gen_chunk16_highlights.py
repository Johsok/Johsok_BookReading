def _fix_english_leaks(lines: list[str]) -> list[str]:
    """Replace accidental English fragments with Traditional Chinese."""
    repl = {
        "conseling": "會談室",
        "Exhaustion": "筋疲力盡",
        "burnout": "耗竭",
        "Reciprocity": "互惠感",
        "ethnocentric": "本位文化偏誤",
        "anima animus": "阿尼瑪與阿尼姆斯",
        "vicarious trauma": "替代性創傷",
        "Fantasia": "幻想",
    }
    out = []
    for s in lines:
        for a, b in repl.items():
            s = s.replace(a, b)
        out.append(s)
    return out


