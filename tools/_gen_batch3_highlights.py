def process_book(book_id: str, category_id: str = "01_business_startup") -> tuple[str, str | None]:
    lines = BOOKS[book_id]
    payload = {"id": book_id, "highlights": numbered(lines)}
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        category_id,
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    if proc.returncode != 0:
        err = (proc.stderr or proc.stdout or "unknown error").strip()
        return book_id, err
    print((proc.stdout or "").strip())
    return book_id, None


def main() -> int:
    target = sys.argv[1] if len(sys.argv) > 1 else None
    ids = [target] if target else list(BOOKS.keys())
    failures = []
    ok = []
    for book_id in ids:
        if book_id not in BOOKS:
            failures.append((book_id, "missing highlights in BOOKS"))
            continue
        bid, err = process_book(book_id)
        if err:
            print(f"FAIL {bid}: {err}", file=sys.stderr)
            failures.append((bid, err))
        else:
            ok.append(bid)
    print("OK:", ok)
    print("FAIL:", failures)
    return 1 if failures else 0


if __name__ == "__main__":
    raise SystemExit(main())
