# fix accidental English
HIGHLIGHTS = [
    h.replace(" Impulsive spending 通常會下降", "衝動消費通常會下降").replace(
        "短期投机話題", "短期投機話題"
    )
    for h in HIGHLIGHTS
]


def main() -> None:
    assert len(HIGHLIGHTS) == 150, len(HIGHLIGHTS)
    assert len(set(HIGHLIGHTS)) == 150
    starts = Counter(h[:18] for h in HIGHLIGHTS)
    assert starts.most_common(1)[0][1] < 4, starts.most_common(5)
    natural = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    bad = []
    for i, body in enumerate(HIGHLIGHTS, 1):
        assert len(body) >= 12
        for p in ("本書", "作者指出", "本章", "這一章", "｜"):
            assert p not in body, (i, p)
        assert not re.search(r".{1,8}面第\d+步[，,]", body)
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(natural):
            bad.append(i)
    assert len(bad) < 3, bad
    payload = {
        "id": BOOK_ID,
        "highlights": [f"{i:03d}、{h}" for i, h in enumerate(HIGHLIGHTS, 1)],
    }
    OUT.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {OUT} count={len(payload['highlights'])}")


if __name__ == "__main__":
    main()
