from findbook_writer import validate_highlights  # noqa: E402


def fmt(items: list[str]) -> list[str]:
    if len(items) != 150:
        raise ValueError(f"need 150, got {len(items)}")
    return [f"{i:03d}、{t}" for i, t in enumerate(items, 1)]


def write_and_complete(book_id: str, items: list[str], title: str, author: str) -> None:
    highlights = fmt(items)
    validate_highlights(book_id, highlights, title, author)
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
    print(proc.stdout, end="")
    if proc.returncode != 0:
        print(proc.stderr, file=sys.stderr)
        raise SystemExit(proc.returncode)
    print(f"OK\t{book_id}\t{len(highlights)}")


