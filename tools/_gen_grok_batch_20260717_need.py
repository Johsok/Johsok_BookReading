    fixes = {
        ("01_business_startup-20260717-105", "B浪若 innovate 新高卻量能不足，要提高假突破警覺"):
            "B浪若創出新高卻量能不足，要提高假突破警覺",
    }
    for book_id, lines in list(BOOKS.items()):
        for old, new in fixes.items():
            if old[0] == book_id and old[1] in lines:
                lines[lines.index(old[1])] = new
        highlights = numbered(lines)
        payload = {"id": book_id, "highlights": highlights}
        out = TOOLS / f".findbook_results_grok_{book_id}.json"
        out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"wrote {out.name}")
        cmd = [
            sys.executable,
            str(TOOLS / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ]
        result = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        print(result.stdout)
        if result.returncode != 0:
            print(result.stderr)
            return result.returncode
    return 0


