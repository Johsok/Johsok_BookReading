def main() -> int:
    for book_id, bodies, title, author in META:
        # fix accidental English leftovers
        bodies = [b.replace(" reciprocal ", "相互").replace(" choking ", "窒息").replace(" extrinsic ", "外部") for b in bodies]
        highlights = pack(bodies)
        validate_highlights(book_id, highlights, title, author)
        out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        out.write_text(json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"wrote\t{out.name}\t{len(highlights)}")
        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            "02_psychology_growth",
            "--results",
            str(out),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        print(proc.stdout, end="")
        if proc.returncode != 0:
            print(proc.stderr, file=sys.stderr)
            raise SystemExit(proc.returncode)
        book_path = ROOT / "Books" / "02_psychology_growth" / f"{book_id}.json"
        book = json.loads(book_path.read_text(encoding="utf-8"))
        status = book.get("chatgptStatus")
        n = len(book.get("chatgptHighlights") or [])
        print(f"verify\t{book_id}\t{status}\t{n}")
        if status != "complete" or n != 150:
            raise SystemExit(f"verify failed: {book_id} status={status} n={n}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
