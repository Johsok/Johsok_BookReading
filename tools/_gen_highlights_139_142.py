def validate_local(book_id: str, highlights: list[str], title: str = "", author: str = "") -> list[str]:
    """Mirror key checks from findbook_writer.validate_highlights."""
    if len(highlights) != 150:
        raise ValueError(f"{book_id} count={len(highlights)}")
    if len(set(highlights)) != 150:
        raise ValueError(f"{book_id} duplicates")
    forbidden = ("本書", "作者指出", "本章", "這一章")
    bodies = []
    short_colon = []
    for i, body in enumerate(highlights, 1):
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short")
        if "｜" in body or "\n" in body:
            raise ValueError(f"{book_id} #{i} bad format")
        if any(p in body for p in forbidden):
            raise ValueError(f"{book_id} #{i} forbidden")
        if "面第" in body and "步" in body:
            raise ValueError(f"{book_id} #{i} step wording")
        import re

        if re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} #{i} step start")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(("是", "為", "在於", "說", "問", "提醒", "表示", "指出")):
            short_colon.append(i)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short colon lines: {short_colon[:10]}")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")
    return [f"{i:03d}、{h}" for i, h in enumerate(highlights, 1)]


def write_and_complete(book_id: str, highlights: list[str], title: str, author: str) -> None:
    lines = validate_local(book_id, highlights, title, author)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": lines}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout.strip())
    if proc.returncode != 0:
        print(proc.stderr, file=sys.stderr)
        raise SystemExit(proc.returncode)


