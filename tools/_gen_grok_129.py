def main() -> int:
    assert len(BODIES) == 150, len(BODIES)
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(BODIES, 1)]
    validate_highlights(BOOK_ID, highlights, TITLE, AUTHOR)
    out = TOOLS / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    print(subprocess.check_output(cmd, cwd=str(ROOT), text=True), end="")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
