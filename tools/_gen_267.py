# fix mixed script issues
fixes = {
    "闭环": "閉環",
    "议程": "議程",
    "Credibility": "可信度",
    "起头": "起頭",
    "出来": "出來",
    "聪明": "聰明",
}
BODIES = [b for b in BODIES]
for i, b in enumerate(BODIES):
    for a, c in fixes.items():
        b = b.replace(a, c)
    BODIES[i] = b

assert len(BODIES) == 150, len(BODIES)
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
short = []
for i, b in enumerate(BODIES, 1):
    m = re.match(r"^([^：:]{1,12})[：:]", b)
    if m and not m.group(1).endswith(NATURAL):
        short.append((i, m.group(1), b[:50]))
starts = Counter(b[:18] for b in BODIES)
title = "你的簡報就是比AI強：47個人類才懂的PPT技巧"
author = "日比野治雄"
print("count", len(BODIES))
print("short_colon", short)
print("bad_starts", [x for x in starts.most_common(5) if x[1] >= 4])
print("dups", len(BODIES) - len(set(BODIES)))
print("title", sum(title in b for b in BODIES), "author", sum(author in b for b in BODIES))
print("min_len", min(len(b) for b in BODIES))
highlights = [f"{i:03d}、{b}" for i, b in enumerate(BODIES, 1)]
