# Fix remaining simplified Chinese in extras
FIXES = [
    ("气流", "氣流"),
    ("冷却液", "冷卻液"),
    ("谐波", "諧波"),
    ("并網", "並網"),
    ("冻结", "凍結"),
    ("修订", "修訂"),
    ("路线图", "路線圖"),
    ("三维指标", "三維指標"),
    ("单一装機", "單一裝機"),
    ("血浆", "血漿"),
    ("叙事", "敘事"),
]


def numbered(lines: list[str]) -> list[str]:
    out = []
    for i, body in enumerate(lines, 1):
        body = body.strip()
        for a, b in FIXES:
            body = body.replace(a, b)
        if body.startswith(f"{i:03d}、"):
            out.append(body)
        else:
            out.append(f"{i:03d}、{body}")
    return out


def main() -> int:
    spec = importlib.util.spec_from_file_location(
        "batch23", ROOT / "tools" / "_gen_batch23_highlights.py"
    )
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)

    success = []
    failures = []
    for book_id, category_id, base_lines in mod.BOOKS:
        extras = EXTRAS[book_id]
        lines = list(base_lines) + list(extras)
        # also apply fixes to base
        fixed = []
        for body in lines:
            for a, b in FIXES:
                body = body.replace(a, b)
            # clean known bad fragments from earlier draft
            body = body.replace(" silently", "").replace("连通", "連通")
            fixed.append(body)
        lines = fixed
        print(f"=== {book_id} total={len(lines)} ===")
        if len(lines) != 150:
            failures.append((book_id, f"count {len(lines)} != 150"))
            continue
        if len(set(lines)) != 150:
            failures.append((book_id, "duplicate bodies before numbering"))
            continue
        highlights = numbered(lines)
        results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        results_path.write_text(
            json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
            + "\n",
            encoding="utf-8",
        )
        proc = subprocess.run(
            [
                sys.executable,
                str(ROOT / "tools" / "findbook_writer.py"),
                "complete",
                "--category-id",
                category_id,
                "--results",
                str(results_path),
            ],
            cwd=str(ROOT),
            capture_output=True,
            text=True,
        )
        out = ((proc.stdout or "") + (proc.stderr or "")).strip()
        print(out)
        if proc.returncode == 0 and "written" in out:
            success.append(book_id)
        else:
            failures.append((book_id, out or f"exit {proc.returncode}"))

    print("SUCCESS:", success)
    print("FAILURES:", failures)
    return 0 if not failures else 1


if __name__ == "__main__":
    raise SystemExit(main())
