ROOT = Path(__file__).resolve().parents[1]
NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN = ("本書", "作者指出", "本章", "這一章")


def validate(book_id: str, points: list[str], title: str, author: str) -> list[str]:
    if len(points) != 150:
        raise SystemExit(f"{book_id} count={len(points)}")
    highlights = []
    bodies = []
    short_colon = []
    for i, body in enumerate(points, 1):
        body = body.strip()
        if len(body) < 12:
            raise SystemExit(f"{book_id} #{i} too short: {body}")
        if "｜" in body or "\n" in body or "\r" in body:
            raise SystemExit(f"{book_id} #{i} forbidden format")
        if any(p in body for p in FORBIDDEN):
            raise SystemExit(f"{book_id} #{i} forbidden prefix")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise SystemExit(f"{book_id} #{i} step wording")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            short_colon.append(i)
        if title and title in body:
            raise SystemExit(f"{book_id} #{i} contains title")
        if author and author in body:
            raise SystemExit(f"{book_id} #{i} contains author")
        highlights.append(f"{i:03d}、{body}")
        bodies.append(body)
    if len(short_colon) >= 3:
        raise SystemExit(f"{book_id} short colon lines: {short_colon[:10]}")
    if len(set(bodies)) != 150:
        raise SystemExit(f"{book_id} duplicate bodies")
    rep = Counter(b[:18] for b in bodies if len(b) >= 18)
    if rep and rep.most_common(1)[0][1] >= 4:
        raise SystemExit(f"{book_id} repeated starts: {rep.most_common(5)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise SystemExit(f"{book_id} repeated {label}")
    return highlights


def write_and_complete(book_id: str, points: list[str], title: str, author: str) -> None:
    highlights = validate(book_id, points, title, author)
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    result = subprocess.run(cmd, cwd=ROOT, capture_output=True, text=True, encoding="utf-8")
    print(result.stdout.strip())
    if result.returncode != 0:
        print(result.stderr, file=sys.stderr)
        raise SystemExit(result.returncode)


