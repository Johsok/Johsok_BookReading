"""Generate 150 ZH-TW highlights for books 39-42 and complete each."""
from __future__ import annotations

import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TOOLS = ROOT / "tools"


def pack(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise ValueError(f"need 150 got {len(bodies)}")
    if len(set(bodies)) != 150:
        raise ValueError("duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"repeated starts: {starts.most_common(3)}")
    for b in bodies:
        if len(b) < 12:
            raise ValueError(f"too short: {b}")
        for bad in ("本書", "作者指出", "本章", "這一章", "｜", "|"):
            if bad in b:
                raise ValueError(f"forbidden: {bad} in {b}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, bodies: list[str]) -> None:
    highlights = pack(bodies)
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--results",
        str(path),
    ]
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout.strip())
    if proc.returncode != 0:
        print(proc.stderr)
        raise SystemExit(proc.returncode)
    book = json.loads((ROOT / "Books" / "01_business_startup" / f"{book_id}.json").read_text(encoding="utf-8"))
    n = len(book.get("chatgptHighlights", []))
    src = book.get("highlightsSource")
    print(f"written/{book_id}/{n}/{src}")


