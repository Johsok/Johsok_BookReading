def write_and_complete(book_id: str, bodies: list[str]) -> None:
    highlights = fmt(bodies)
    # uniqueness / length sanity
    if len(set(bodies)) != len(bodies):
        from collections import Counter

        c = Counter(bodies)
        dups = [k for k, v in c.items() if v > 1]
        raise SystemExit(f"{book_id} duplicate bodies: {dups[:5]}")
    short = [b for b in bodies if len(b) < 12]
    if short:
        raise SystemExit(f"{book_id} short bodies: {short[:3]}")

    results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights, "source": "grok"}
    results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "--root",
        str(ROOT),
        "complete",
        "--results",
        str(results_path),
    ]
    print(f"RUN {book_id} count={len(highlights)}")
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
    sys.stdout.write(proc.stdout)
    sys.stderr.write(proc.stderr)
    if proc.returncode != 0:
        raise SystemExit(f"{book_id} writer failed: {proc.returncode}")

    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    n = len(saved.get("chatgptHighlights", []))
    status = saved.get("chatgptStatus")
    source = saved.get("highlightsSource")
    print(f"VERIFY\t{book_id}\tcount={n}\tstatus={status}\tsource={source}")
    if n != 150 or status != "complete" or source != "grok":
        raise SystemExit(f"{book_id} verification failed")


def main() -> None:
    for book_id, bodies in BOOKS.items():
        write_and_complete(book_id, bodies)
