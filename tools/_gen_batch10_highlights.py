    for book_id, category_id, title, author, raw_lines in books:
        highlights = pack(raw_lines)
        try:
            validate_local(book_id, highlights, title=title, author=author)
        except ValueError as exc:
            failed.append((book_id, f"local validate: {exc}"))
            continue

        results_path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
        payload = {"id": book_id, "highlights": highlights}
        results_path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

        cmd = [
            sys.executable,
            str(ROOT / "tools" / "findbook_writer.py"),
            "complete",
            "--category-id",
            category_id,
            "--results",
            str(results_path),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
        if proc.returncode != 0:
            err = (proc.stderr or proc.stdout or "writer failed").strip()
            failed.append((book_id, err))
            print(f"FAIL {book_id}: {err}")
            continue
        written.append(book_id)
        print(f"OK {book_id}: {(proc.stdout or '').strip()}")

    print("---")
    print("written:", written)
    print("failed:", failed)
    return 0 if not failed else 1


