def validate_local(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id}: need 150, got {len(highlights)}")
    bodies: list[str] = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"{book_id} #{index} bad number")
        body = line[len(expected) :].strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} #{index} too short")
        for prefix in ("本書", "作者指出", "本章", "這一章"):
            if prefix in body:
                raise ValueError(f"{book_id} #{index} forbidden prefix")
        if "｜" in body or "\n" in body:
            raise ValueError(f"{book_id} #{index} bad format")
        bodies.append(body)
    if len(set(bodies)) != len(bodies):
        raise ValueError(f"{book_id}: duplicate bodies")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id}: repeated starts {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id}: {label} appears too often")


def pack(lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise ValueError(f"expected 150 raw lines, got {len(lines)}")
    return [f"{i:03d}、{text}" for i, text in enumerate(lines, 1)]


