NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")


def main() -> None:
    hs = [h.replace("动摇", "動搖").replace("consum ", "消耗").replace("动作", "動作").replace("晒網", "晒網") for h in HIGHLIGHTS]
    # fix bad replacements
    hs = []
    for h in HIGHLIGHTS:
        h = h.replace("动摇", "動搖").replace("动作", "動作")
        h = h.replace("受害者敘事 consum 注意力", "受害者敘事會耗盡注意力")
        h = h.replace("三天漁兩天晒網", "三天打魚兩天晒網")
        m = re.match(r"^([^：:]{1,12})[：:]", h)
        if m and not m.group(1).endswith(NATURAL):
            h = h.replace("：", "，", 1).replace(":", "，", 1)
        hs.append(h)
    assert len(hs) == 150, len(hs)
    assert len(set(hs)) == 150, len(set(hs))
    starts = Counter(h[:18] for h in hs)
    assert starts.most_common(1)[0][1] < 4, starts.most_common(5)
    payload = {"id": BOOK_ID, "highlights": [f"{i:03d}、{h}" for i, h in enumerate(hs, 1)]}
    path = Path(__file__).with_name(f".findbook_results_grok_{BOOK_ID}.json")
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print("wrote", path, len(payload["highlights"]))


