ROOT = Path(__file__).resolve().parents[1]
TOOLS = ROOT / "tools"
WRITER = TOOLS / "findbook_writer.py"

NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL_COLON_SUFFIXES = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN_PREFIXES = ("本書", "作者指出", "本章", "這一章")


def validate_local(book_id: str, highlights: list[str], title: str, author: str) -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id} count={len(highlights)}")
    short_colon = []
    bodies = []
    for index, line in enumerate(highlights, 1):
        expected = f"{index:03d}、"
        if not line.startswith(expected):
            raise ValueError(f"{book_id} #{index} numbering")
        if "\n" in line or "\r" in line or "｜" in line:
            raise ValueError(f"{book_id} #{index} forbidden format")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} #{index} too short: {body}")
        if any(p in body for p in FORBIDDEN_PREFIXES):
            raise ValueError(f"{book_id} #{index} forbidden prefix")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} #{index} step wording")
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(NATURAL_COLON_SUFFIXES):
            short_colon.append(index)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short-colon lines: {short_colon[:10]}")
    if len(set(bodies)) != 150:
        raise ValueError(f"{book_id} duplicates")
    repeated = Counter(b[:18] for b in bodies)
    top = repeated.most_common(1)[0]
    if top[1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {top}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeated {label}")


def write_and_complete(book_id: str, bodies: list[str], title: str, author: str) -> None:
    highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
    validate_local(book_id, highlights, title, author)
    payload = {"id": book_id, "highlights": highlights}
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(WRITER),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    result = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    print(result.stdout)
    if result.returncode != 0:
        print(result.stderr)
        raise SystemExit(result.returncode)
    print(f"DONE {book_id} {len(highlights)}")


