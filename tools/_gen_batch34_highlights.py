def main() -> int:
    book66, book67 = load_book66_67()
    bodies_map = {
        "02_psychology_growth-20260718-66": book66,
        "02_psychology_growth-20260718-67": book67,
        "02_psychology_growth-20260718-68": BOOK68,
        "02_psychology_growth-20260718-69": BOOK69,
        "02_psychology_growth-20260718-70": BOOK70,
    }
    written = []
    failed = []
    for book_id, _bodies, title, author in META:
        bodies = bodies_map[book_id]
        # Strip accidental English leftovers
        bodies = [
            b.replace(" Empathy ", "同理")
            .replace(" rutin ", "例行")
            .replace(" Impuls ", "衝動")
            .replace(" overlapping ", "重疊")
            .replace(" dignified ", "有尊嚴地")
            for b in bodies
        ]
        try:
            highlights = pack(bodies)
            validate_highlights(book_id, highlights, title, author)
            out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
            out.write_text(
                json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
                encoding="utf-8",
            )
            print(f"wrote\t{out.name}\t{len(highlights)}")
            cmd = [
                sys.executable,
                str(ROOT / "tools" / "findbook_writer.py"),
                "complete",
                "--category-id",
                "02_psychology_growth",
                "--results",
                str(out),
            ]
            proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
            print(proc.stdout, end="")
            if proc.returncode != 0:
                err = (proc.stderr or proc.stdout or "").strip()
                failed.append((book_id, err))
                print(err, file=sys.stderr)
                continue
            book_path = ROOT / "Books" / "02_psychology_growth" / f"{book_id}.json"
            book = json.loads(book_path.read_text(encoding="utf-8"))
            status = book.get("chatgptStatus")
            n = len(book.get("chatgptHighlights") or [])
            src = book.get("highlightsSource")
            print(f"verify\t{book_id}\t{status}\t{n}\t{src}")
            if status != "complete" or n != 150 or src != "grok":
                failed.append((book_id, f"verify failed status={status} n={n} src={src}"))
            else:
                written.append(book_id)
        except Exception as exc:
            failed.append((book_id, str(exc)))
            print(f"error\t{book_id}\t{exc}", file=sys.stderr)
    print("---")
    print("written:", ", ".join(written) if written else "(none)")
    if failed:
        print("failed:")
        for bid, err in failed:
            print(f"  {bid}: {err}")
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
