def run_one(book_id: str, bodies: list[str]) -> tuple[bool, str]:
    book_path = BOOK_DIR / f"{book_id}.json"
    book = json.loads(book_path.read_text(encoding="utf-8-sig"))
    title = str(book.get("title", ""))
    author = str(book.get("author", ""))
    highlights = numbered(bodies)
    try:
        validate_highlights(book_id, highlights, title, author)
    except Exception as e:
        return False, f"validate: {e}"

    out = ROOT / "tools" / f".findbook_result_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    try:
        r = subprocess.run(
            [sys.executable, str(COMPLETE), "--results", str(out)],
            cwd=str(ROOT),
            capture_output=True,
            text=True,
            encoding="utf-8",
            errors="replace",
        )
    except Exception as e:
        return False, f"complete exec: {e}"

    if r.returncode != 0:
        err = (r.stderr or r.stdout or "").strip()
        return False, f"complete rc={r.returncode}: {err[:500]}"
    return True, (r.stdout or "").strip()


def main() -> int:
    # Books 01-02 from rich banks; 03-10 generated in companion function below
    from _gen_grok_02_banks_rest import BANKS_03_TO_10  # type: ignore

    mapping = {
        "02_psychology_growth-20260719-01": bank_influence(),
        "02_psychology_growth-20260719-02": bank_conflict_emotion(),
        **BANKS_03_TO_10,
    }

    ok_ids: list[str] = []
    fail: list[tuple[str, str]] = []

    for book_id, bodies in mapping.items():
        print(f"=== {book_id} gen={len(bodies)} ===")
        success, msg = run_one(book_id, bodies)
        if success:
            print(f"OK {msg}")
            ok_ids.append(book_id)
        else:
            print(f"FAIL {msg}")
            # one retry: drop short / diversify by reversing
            retry_bodies = list(reversed(bodies)) + [b + "。" if not b.endswith("。") else b for b in bodies[:20]]
            success2, msg2 = run_one(book_id, retry_bodies)
            if success2:
                print(f"OK-retry {msg2}")
                ok_ids.append(book_id)
            else:
                print(f"FAIL-retry {msg2}")
                fail.append((book_id, msg2))

    print("SUCCESS", ok_ids)
    print("FAIL", fail)
    return 0 if not fail else 1


