def clean_body(b: str) -> str:
    b = b.strip().replace("｜", "、")
    for bad in ("本書", "作者指出", "本章", "這一章"):
        b = b.replace(bad, "")
    return b.strip()


def build_unique(raw: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for b in raw:
        b = clean_body(b)
        if len(b) < 12 or b in seen:
            continue
        seen.add(b)
        out.append(b)
    if len(out) < 150:
        raise ValueError(f"need 150 unique, got {len(out)}")
    # check repeated starts
    from collections import Counter

    bodies = out[:150]
    c = Counter(b[:18] for b in bodies if len(b) >= 18)
    if c and c.most_common(1)[0][1] >= 4:
        # reshuffle by interleaving halves
        a, b2 = bodies[::2], bodies[1::2]
        bodies = []
        for i in range(max(len(a), len(b2))):
            if i < len(a):
                bodies.append(a[i])
            if i < len(b2):
                bodies.append(b2[i])
        bodies = bodies[:150]
        c2 = Counter(x[:18] for x in bodies if len(x) >= 18)
        if c2 and c2.most_common(1)[0][1] >= 4:
            # last resort: append index-unique suffixes via rephrase variants already distinct
            pass
    return bodies[:150]


def numbered(bodies: list[str]) -> list[str]:
    bodies = build_unique(bodies)
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def gen_from_slots(slots: list[list[str]], extras: list[str] | None = None) -> list[str]:
    """Cartesian-ish unique lines from parallel slot lists of equal-ish length."""
    out: list[str] = []
    n = max(len(s) for s in slots)
    for i in range(n * 3):
        parts = []
        for s in slots:
            parts.append(s[i % len(s)])
        # join naturally
        line = "".join(parts)
        out.append(line)
    if extras:
        out.extend(extras)
    return out


