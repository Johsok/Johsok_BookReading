BOOK_ID = "01_business_startup-20260717-152"
TITLE = "新金融怪傑：頂尖操盤手的致勝之鑰(30周年紀念版)(上)"
AUTHOR = "傑克．史瓦格"


def validate(highlights: list[str]) -> None:
    if len(highlights) != 150:
        raise ValueError(f"count {len(highlights)}")
    short = []
    bodies = []
    for i, line in enumerate(highlights, 1):
        if not line.startswith(f"{i:03d}、"):
            raise ValueError(f"num {i}")
        if "｜" in line or "\n" in line:
            raise ValueError(f"fmt {i}")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"short {i}")
        if any(p in body for p in FORBIDDEN):
            raise ValueError(f"forbid {i}")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"step {i}")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            short.append(i)
        bodies.append(body)
    if len(short) >= 3:
        raise ValueError(f"colon {short}")
    if len(set(bodies)) != len(bodies):
        raise ValueError("dup")
    starts = Counter(b[:18] for b in bodies)
    if starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"starts {starts.most_common(3)}")
    if sum(TITLE in b for b in bodies) >= 2 or sum(AUTHOR in b for b in bodies) >= 2:
        raise ValueError("title/author")


def main() -> None:
    print("items", len(ITEMS), "unique", len(set(ITEMS)))
    highlights = [f"{i:03d}、{t}" for i, t in enumerate(ITEMS, 1)]
    validate(highlights)
    out = TOOLS / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    proc = subprocess.run(
        [
            sys.executable,
            str(TOOLS / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ],
        cwd=str(ROOT),
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    sys.stdout.write(proc.stdout or "")
    sys.stderr.write(proc.stderr or "")
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    print(f"DONE\t{BOOK_ID}\t{len(highlights)}")


if __name__ == "__main__":
    main()
