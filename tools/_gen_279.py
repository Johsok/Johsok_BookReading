# fix short colons
import re
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
fixed = []
for h in HIGHLIGHTS:
    m = re.match(r"^([^：:]{1,12})[：:]", h)
    if m and not m.group(1).endswith(NATURAL):
        h = h.replace("：", "，", 1).replace(":", "，", 1)
    fixed.append(h)

