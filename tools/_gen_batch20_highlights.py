"""Generate findbook results JSON for batch20 books."""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def numbered(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150, got {len(bodies)}")
    return [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]


def write_results(book_id: str, bodies: list[str]) -> Path:
    path = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": numbered(bodies)}
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return path


