def scrub(bodies: list[str]) -> list[str]:
    """Rewrite short-label colons so validate_highlights passes."""
    out = []
    for body in bodies:
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL_COLON_SUFFIXES):
            # turn first colon into pause comma
            body = body[: m.end(1)] + "，" + body[m.end() :]
        out.append(body)
    return out

def pack(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150 got {len(bodies)}")
    if len(set(bodies)) != 150:
        dups = [b for b, c in Counter(bodies).items() if c > 1]
        raise SystemExit(f"dups: {dups[:3]}")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    bad = [(s, n) for s, n in starts.most_common() if n >= 4]
    if bad:
        raise SystemExit(f"repeated starts: {bad[:8]}")
    for i, b in enumerate(bodies, 1):
        if len(b) < 12:
            raise SystemExit(f"#{i} short: {b}")
        if "｜" in b or any(x in b for x in ("本書", "作者指出", "本章", "這一章")):
            raise SystemExit(f"#{i} forbidden: {b}")
        if re.search(r".{1,8}面第\d+步[，,]", b) or re.match(r"^第\d+步[，,]", b):
            raise SystemExit(f"#{i} step junk: {b}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


