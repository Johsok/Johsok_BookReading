ROOT = Path(__file__).resolve().parents[1]
TOOLS = ROOT / "tools"


def validate_local(book_id: str, bodies: list[str], title: str = "", author: str = "") -> list[str]:
    """Validate highlight bodies before writing results JSON."""
    if len(bodies) != 150:
        raise ValueError(f"{book_id} count={len(bodies)}")
    if len(set(bodies)) != 150:
        raise ValueError(f"{book_id} duplicate bodies")
    for i, body in enumerate(bodies, 1):
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short")
        for bad in ("本書", "作者指出", "本章", "這一章", "｜"):
            if bad in body:
                raise ValueError(f"{book_id} #{i} forbidden: {bad}")
        if "\n" in body or "\r" in body:
            raise ValueError(f"{book_id} #{i} newline")
    starts = Counter(b[:18] for b in bodies)
    if starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats {label}")
    return [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]


def write_and_complete(book_id: str, highlights: list[str]) -> None:
    """Write results JSON and run findbook_writer complete."""
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(path),
    ]
    result = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    print(result.stdout.strip())
    if result.returncode != 0:
        print(result.stderr)
        raise SystemExit(result.returncode)


