def clean_bodies(bodies: list[str]) -> list[str]:
    """Remove accidental junk lines and normalize text."""
    cleaned = []
    for b in bodies:
        b = b.strip()
        if not b:
            continue
        # drop broken draft lines
        if " ent " in b or b.endswith("',") or "tolérance" in b or "复購" in b or "适度" in b or "却" in b:
            b = (
                b.replace(" ent ", " ")
                .replace("',", "")
                .replace("tolérance", "耐受度")
                .replace("复購", "回購")
                .replace("适度", "適度")
                .replace("却", "卻")
            )
        # fix known bad lines by skipping duplicates of fixed versions
        cleaned.append(b)
    return cleaned


