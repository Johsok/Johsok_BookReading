def main() -> int:
    for book_id, author, title, items in BOOKS:
        items = scrub(items)
        if len(items) != 150:
            # show count for debugging
            raise SystemExit(f"{book_id} has {len(items)} items")
        highlights = numbered(items)
        validate(book_id, highlights, title=title, author=author)
        out = TOOLS / f".findbook_results_grok_{book_id}.json"
        payload = {"id": book_id, "highlights": highlights}
        out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        cmd = [
            sys.executable,
            str(TOOLS / "findbook_writer.py"),
            "complete",
            "--category-id",
            "01_business_startup",
            "--results",
            str(out),
        ]
        proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
        sys.stdout.write(proc.stdout)
        if proc.returncode != 0:
            sys.stderr.write(proc.stderr)
            raise SystemExit(f"complete failed for {book_id}: {proc.returncode}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
