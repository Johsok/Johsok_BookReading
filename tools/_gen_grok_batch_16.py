NUMBER_RE = re.compile(r"^\d{3}、")
NATURAL = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
FORBIDDEN = ("本書", "作者指出", "本章", "這一章")


def validate(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"{book_id} count={len(highlights)}")
    short_colon = []
    bodies = []
    for i, line in enumerate(highlights, 1):
        exp = f"{i:03d}、"
        if not isinstance(line, str) or not line.startswith(exp):
            raise ValueError(f"{book_id} #{i} numbering")
        if "\n" in line or "\r" in line or "｜" in line or "|" in line:
            raise ValueError(f"{book_id} #{i} forbidden format")
        body = NUMBER_RE.sub("", line, count=1).strip()
        if len(body) < 12:
            raise ValueError(f"{book_id} #{i} too short: {body}")
        if any(p in body for p in FORBIDDEN):
            raise ValueError(f"{book_id} #{i} forbidden prefix")
        if re.search(r".{1,8}面第\d+步[，,]", body) or re.match(r"^第\d+步[，,]", body):
            raise ValueError(f"{book_id} #{i} step wording")
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            short_colon.append(i)
        bodies.append(body)
    if len(short_colon) >= 3:
        raise ValueError(f"{book_id} short-colon lines: {short_colon[:12]}")
    if len(set(bodies)) != len(bodies):
        dups = [b for b in bodies if bodies.count(b) > 1]
        raise ValueError(f"{book_id} duplicates: {dups[:2]}")
    rep = Counter(b[:18] for b in bodies if len(b) >= 18)
    if rep and rep.most_common(1)[0][1] >= 4:
        raise ValueError(f"{book_id} repeated starts: {rep.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{book_id} repeats full {label}")


def numbered(items: list[str]) -> list[str]:
    if len(items) != 150:
        raise ValueError(f"need 150 got {len(items)}")
    return [f"{i:03d}、{t}" for i, t in enumerate(items, 1)]


