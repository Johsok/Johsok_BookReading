# -*- coding: utf-8 -*-
"""Generate FindBook Grok highlights for batch1 books 29-30."""
import importlib.util
import json
from pathlib import Path

spec = importlib.util.spec_from_file_location("fw", "tools/findbook_writer.py")
fw = importlib.util.module_from_spec(spec)
spec.loader.exec_module(fw)

ROOT = Path(".")
BATCH = json.loads((ROOT / "tools/_batch1.json").read_text(encoding="utf-8"))


def pack(book_id: str, ideas: list[str]) -> None:
    assert len(ideas) == 150, f"{book_id} count={len(ideas)}"
    assert len(set(ideas)) == 150, f"{book_id} duplicate bodies"
    book = next(b for b in BATCH if b["id"] == book_id)
    highlights = [f"{i:03d}、{s}" for i, s in enumerate(ideas, 1)]
    fw.validate_highlights(book_id, highlights, book["title"], book["author"])
    out = ROOT / f"tools/.findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print("ok", book_id)


