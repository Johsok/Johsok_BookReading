def check(highlights, title, author):
    """Validate highlight list against writer rules."""
    assert len(highlights) == 150
    natural = ("是", "為", "在於", "說", "問", "提醒", "表示", "指出")
    bad = []
    for i, h in enumerate(highlights, 1):
        assert h.startswith(f"{i:03d}、"), (i, h[:50])
        body = h.split("、", 1)[1]
        assert len(body) >= 12, i
        assert "｜" not in body and "\n" not in body
        for pfx in ("本書", "作者指出", "本章", "這一章"):
            assert pfx not in body, (i, pfx)
        assert not re.search(r".{1,8}面第\d+步[，,]", body)
        assert not re.match(r"^第\d+步[，,]", body)
        match = re.match(r"^([^：:]{1,12})[：:]", body)
        if match and not match.group(1).endswith(natural):
            bad.append(i)
    bodies = [h.split("、", 1)[1] for h in highlights]
    assert len(set(bodies)) == 150
    counts = Counter(b[:18] for b in bodies)
    assert counts.most_common(1)[0][1] < 4, counts.most_common(5)
    assert sum(title in b for b in bodies) < 2
    assert sum(author in b for b in bodies) < 2
    assert len(bad) < 3, bad
    return bad


def write_results(book_id, highlights):
    """Write results JSON for findbook_writer complete."""
    path = TOOLS / f".findbook_results_grok_{book_id}.json"
    path.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print("wrote", path.name, len(highlights))


