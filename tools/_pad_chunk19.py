def clean(s: str) -> str:
    s = COLON_REWRITES.get(s, s)
    s = (
        s.replace("绑架", "綁架")
        .replace("觉得", "覺得")
        .replace("张开", "張開")
        .replace("荒谬", "荒謬")
        .replace("自愿不适", "自願不適")
        .replace("第三条路", "第三條路")
        .replace("叠加", "疊加")
    )
    s = re.sub(r"\bfortify\b", "加固", s)
    s = re.sub(r"\bretrospective\b", "回顧", s)
    # neutralize remaining short-label colons
    m = re.match(r"^([^：:]{1,12})[：:](.*)$", s)
    if m and not m.group(1).endswith(NATURAL):
        s = m.group(1) + "，" + m.group(2)
    return s


def pack(lines: list[str]) -> list[str]:
    return [f"{i:03d}、{text}" for i, text in enumerate(lines, 1)]


def short_colon_count(lines: list[str]) -> list[int]:
    bad = []
    for i, body in enumerate(lines, 1):
        m = re.match(r"^([^：:]{1,12})[：:]", body)
        if m and not m.group(1).endswith(NATURAL):
            bad.append(i)
    return bad


