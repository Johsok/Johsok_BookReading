def bodies_to_highlights(bodies: list[str]) -> list[str]:
    if len(bodies) != 150:
        raise SystemExit(f"need 150, got {len(bodies)}")
    return [f"{i:03d}、{body}" for i, body in enumerate(bodies, 1)]


def run_one(book_id: str) -> None:
    bodies = BOOKS[book_id]
    highlights = bodies_to_highlights(bodies)
    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    book = json.loads(book_path.read_text(encoding="utf-8-sig"))
    validate_highlights(book_id, highlights, book.get("title", ""), book.get("author", ""))
    out = TOOLS / f".findbook_results_grok_{book_id}.json"
    out.write_text(
        json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(TOOLS / "findbook_writer.py"),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(out),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True)
    sys.stdout.write(proc.stdout)
    sys.stderr.write(proc.stderr)
    if proc.returncode != 0:
        raise SystemExit(proc.returncode)
    # verify count
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    n = len(saved.get("chatgptHighlights") or [])
    status = saved.get("chatgptStatus")
    print(f"verify\t{book_id}\t{n}\t{status}")


def main() -> None:
    ids = sys.argv[1:] or list(BOOKS.keys())
    for book_id in ids:
        if book_id not in BOOKS:
            raise SystemExit(f"unknown id {book_id}")
        print(f"=== {book_id} ===")
        run_one(book_id)


if __name__ == "__main__":
    main()
