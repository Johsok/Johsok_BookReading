def load_book_lists() -> dict[str, list[str]]:
    code = (ROOT / "tools" / "_gen_batch10_highlights.py").read_text(encoding="utf-8")
    mod = ast.parse(code)
    books: dict[str, list[str]] = {}
    for node in mod.body:
        if isinstance(node, ast.Assign):
            for t in node.targets:
                if isinstance(t, ast.Name) and t.id.startswith("BOOK"):
                    books[t.id] = list(ast.literal_eval(node.value))
    for key, extra in EXTRA.items():
        books[key] = books[key] + extra
    return books


def validate_local(book_id: str, highlights: list[str], title: str = "", author: str = "") -> None:
    if len(highlights) != 150:
        raise ValueError(f"need 150 got {len(highlights)}")
    bodies: list[str] = []
    for i, line in enumerate(highlights, 1):
        exp = f"{i:03d}、"
        if not line.startswith(exp):
            raise ValueError(f"#{i} bad num")
        body = line[len(exp) :].strip()
        if len(body) < 12:
            raise ValueError(f"#{i} short: {body}")
        for p in ("本書", "作者指出", "本章", "這一章"):
            if p in body:
                raise ValueError(f"#{i} forbidden")
        if "｜" in body or "\n" in body:
            raise ValueError(f"#{i} format")
        bodies.append(body)
    if len(set(bodies)) != len(bodies):
        c = Counter(bodies)
        dups = [b for b, n in c.items() if n > 1]
        raise ValueError(f"dups: {dups[:3]}")
    starts = Counter(b[:18] for b in bodies if len(b) >= 18)
    if starts and starts.most_common(1)[0][1] >= 4:
        raise ValueError(f"repeated starts {starts.most_common(3)}")
    for label, value in (("title", title), ("author", author)):
        if value and sum(value in b for b in bodies) >= 2:
            raise ValueError(f"{label} repeated")


