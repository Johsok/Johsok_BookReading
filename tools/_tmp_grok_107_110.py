def numbered(lines: list[str]) -> list[str]:
    if len(lines) != 150:
        raise SystemExit(f"expected 150, got {len(lines)}")
    return [f"{i:03d}、{text}" for i, text in enumerate(lines, 1)]


def complete_book(book_id: str, lines: list[str]) -> None:
    """Write results JSON and run findbook_writer complete."""
    highlights = numbered(lines)
    results_path = TOOLS / f".findbook_results_grok_{book_id}.json"
    payload = {"id": book_id, "highlights": highlights}
    results_path.write_text(
        json.dumps(payload, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    cmd = [
        sys.executable,
        str(WRITER),
        "complete",
        "--category-id",
        "01_business_startup",
        "--results",
        str(results_path),
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    sys.stdout.write(proc.stdout or "")
    if proc.returncode != 0:
        sys.stderr.write(proc.stderr or "")
        raise SystemExit(proc.returncode)
    book_path = ROOT / "Books" / "01_business_startup" / f"{book_id}.json"
    saved = json.loads(book_path.read_text(encoding="utf-8-sig"))
    n = len(saved.get("chatgptHighlights", []))
    src = saved.get("highlightsSource")
    print(f"report\twritten/{book_id}/{n}/{src}")


def main() -> None:
    mapping = {
        "107": "01_business_startup-20260716-107",
        "108": "01_business_startup-20260716-108",
        "109": "01_business_startup-20260716-109",
        "110": "01_business_startup-20260716-110",
        "all": "all",
    }
    which = sys.argv[1] if len(sys.argv) > 1 else "all"
    data = books()
    if which == "all":
        for book_id in data:
            print(f"=== {book_id} count={len(data[book_id])} ===")
            complete_book(book_id, data[book_id])
        return
    book_id = mapping[which]
    print(f"=== {book_id} count={len(data[book_id])} ===")
    complete_book(book_id, data[book_id])


if __name__ == "__main__":
    main()
