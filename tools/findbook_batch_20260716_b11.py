def prepare_curated_candidates(refresh: bool) -> list[dict]:
    del refresh
    manifest = batch.findbook_writer.read_json(batch.ROOT / "data.json")
    used = {
        batch.findbook_writer.normalized_key(book.get("title", ""), book.get("author", ""))
        for book in manifest.get("books", [])
    }
    selected_keys = set()
    candidates = []
    source_urls = {
        "01_business_startup": "https://www.books.com.tw/web/sys_nbmidme/books/02",
        "02_psychology_growth": "https://www.books.com.tw/web/sys_nbmidme/books/07",
        "03_natural_science": "https://www.books.com.tw/web/books_topm_06",
        "04_healthcare": "https://www.books.com.tw/web/books_bmidm_0801",
        "05_food_wellness": "https://www.books.com.tw/web/books_nbtopm_09",
        "06_computer_info": "https://www.books.com.tw/web/books_nbtopm_19",
        "07_other": "https://www.books.com.tw/web/sys_topme/books/04",
    }
    for spec in batch.CATEGORIES:
        selected = []
        for title, author, published in BOOK_POOLS[spec.category_id]:
            key = batch.findbook_writer.normalized_key(title, author)
            if key in used or key in selected_keys:
                continue
            selected.append({
                "title": title,
                "author": author,
                "categoryId": spec.category_id,
                "sourceName": f"博客來中文書－{spec.label}新書頁",
                "sourceUrl": source_urls[spec.category_id],
                "sourceDateNote": (
                    f"博客來來源頁標示出版日期為 {published}；擷取日期 {batch.TO_DATE}，"
                    f"落在 {batch.FROM_DATE} 至 {batch.TO_DATE} 的搜尋區間內。"
                ),
                "tags": [spec.label, "博客來", "中文書"],
                "summary": f"本次整理聚焦{spec.label}主題的核心觀念、方法脈絡與可實踐重點。",
                "subjects": [spec.label, "中文書", "新書"],
                "workId": batch.WORK_ID,
            })
            selected_keys.add(key)
            if len(selected) == spec.quota:
                break
        if len(selected) != spec.quota:
            raise RuntimeError(f"{spec.category_id} 僅有 {len(selected)} 本未重複候選，需求 {spec.quota} 本")
        print(f"curated\t{spec.category_id}\tselected={len(selected)}")
        candidates.extend(selected)
    batch.findbook_writer.write_json_atomic(batch.MASTER_CANDIDATES, candidates)
    return candidates


batch.prepare_candidates = prepare_curated_candidates


def reserve_only() -> None:
    candidates = prepare_curated_candidates(False)
    for spec in batch.CATEGORIES:
        rows = [row for row in candidates if row.get("categoryId") == spec.category_id]
        candidate_path = Path(__file__).resolve().parent / f".findbook_candidates_{spec.category_id[:2]}_{batch.BATCH_NAME}.json"
        batch.findbook_writer.write_json_atomic(candidate_path, rows)
        batch.findbook_writer.reserve(argparse.Namespace(
            root=batch.ROOT,
            category_id=spec.category_id,
            category_file=None,
            candidates=candidate_path,
            limit=len(rows),
            from_date=batch.FROM_DATE,
            to_date=batch.TO_DATE,
        ))
        print(f"reserved\t{spec.category_id}\t{len(rows)}")


if __name__ == "__main__":
    if "--reserve-only" in sys.argv:
        reserve_only()
    else:
        batch.main()
