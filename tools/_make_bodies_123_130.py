def main() -> int:
    # load remaining theme builders from sibling modules if present
    for mod_name in ("_seeds_125_130",):
        path = TOOLS / f"{mod_name}.py"
        if path.exists():
            import importlib.util

            spec = importlib.util.spec_from_file_location(mod_name, path)
            mod = importlib.util.module_from_spec(spec)
            assert spec.loader
            spec.loader.exec_module(mod)
            THEMES.update(mod.THEMES)

    written = []
    for book_id, builder in THEMES.items():
        bodies = builder()
        emit_module(book_id, bodies)
        highlights = [f"{i:03d}、{b}" for i, b in enumerate(bodies, 1)]
        book = json.loads((ROOT / "Books/01_business_startup" / f"{book_id}.json").read_text(encoding="utf-8-sig"))
        validate_highlights(book_id, highlights, book.get("title", ""), book.get("author", ""))
        out = TOOLS / f".findbook_results_grok_{book_id}.json"
        out.write_text(json.dumps({"id": book_id, "highlights": highlights}, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        proc = subprocess.run(
            [sys.executable, str(TOOLS / "findbook_writer.py"), "complete", "--category-id", "01_business_startup", "--results", str(out)],
            cwd=str(ROOT),
            capture_output=True,
            text=True,
            encoding="utf-8",
        )
        sys.stdout.write(proc.stdout)
        if proc.returncode != 0:
            sys.stderr.write(proc.stderr or "")
            print(f"FAILED {book_id}", file=sys.stderr)
            return proc.returncode
        written.append(book_id)
        print(f"status\t{book_id}\twritten")
    print(f"TOTAL written={len(written)}")
    return 0


