def emit_module(book_id: str, bodies: list[str]) -> None:
    if len(bodies) != 150:
        raise ValueError(f"{book_id}: {len(bodies)}")
    path = TOOLS / f"_bodies_{book_id.split('-')[-1]}.py"
    lines = ["# -*- coding: utf-8 -*-", "BOOKS = {", f'    "{book_id}": [']
    for b in bodies:
        lines.append(f"        {json.dumps(b, ensure_ascii=False)},")
    lines.append("    ],")
    lines.append("}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


