"""Generate and complete findbook highlights for books 131-134."""
from __future__ import annotations

import json
import subprocess
import sys
from collections import Counter
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]


def write_and_complete(book_id: str, lines: list[str]) -> None:
    """Validate, write results JSON, then run findbook_writer complete."""
    assert len(lines) == 150, (book_id, len(lines))
    assert len(set(lines)) == 150, book_id
    starts = Counter(h[:18] for h in lines)
    if starts.most_common(1)[0][1] >= 4:
        raise SystemExit(f"{book_id} repeated starts: {starts.most_common(5)}")
    payload = {
        "id": book_id,
        "highlights": [f"{i:03d}、{h}" for i, h in enumerate(lines, 1)],
    }
    out = ROOT / "tools" / f".findbook_results_grok_{book_id}.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    cmd = [
        sys.executable,
        str(ROOT / "tools" / "findbook_writer.py"),
        "complete",
        "--results",
        str(out),
        "--category-id",
        "01_business_startup",
    ]
    proc = subprocess.run(cmd, cwd=str(ROOT), capture_output=True, text=True, encoding="utf-8")
    print(proc.stdout, end="")
    if proc.returncode != 0:
        print(proc.stderr, file=sys.stderr)
        raise SystemExit(proc.returncode)


