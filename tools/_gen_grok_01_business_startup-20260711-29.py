def main() -> None:
    if len(BODIES) != 150:
        raise SystemExit(f"need 150 bodies, got {len(BODIES)}")
    short = [b for b in BODIES if len(b) < 12]
    if short:
        raise SystemExit(f"short bodies: {short}")
    if len(set(BODIES)) != 150:
        raise SystemExit("duplicate bodies")
    highlights = [f"{i:03d}、{body}" for i, body in enumerate(BODIES, 1)]
    validate_highlights(BOOK_ID, highlights, TITLE, AUTHOR)
    out = ROOT / "tools" / f".findbook_results_grok_{BOOK_ID}.json"
    out.write_text(
        json.dumps({"id": BOOK_ID, "highlights": highlights}, ensure_ascii=False, indent=2)
        + "\n",
        encoding="utf-8",
    )
    print(f"wrote {out} count={len(highlights)}")


if __name__ == "__main__":
    main()
