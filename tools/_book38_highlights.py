def main():
    assert len(BOOK38) == 150, len(BOOK38)
    forbidden = ["本書", "作者指出", "本章", "這一章", "｜", "茲米格羅德", "Zmigrod", "莉奧", "別讓意識型態害了你", "Ideological"]
    for i, s in enumerate(BOOK38):
        assert isinstance(s, str)
        assert len(s) >= 12, (i, len(s), s)
        for f in forbidden:
            assert f not in s, (i, f, s)
        # avoid short-label-colon
        if "：" in s:
            left = s.split("：", 1)[0]
            assert len(left) >= 8, (i, "short-colon", s)
    opens = [s[:18] for s in BOOK38]
    c = Counter(opens)
    bad = [(k, v) for k, v in c.items() if v >= 4]
    assert not bad, bad
    assert len(set(BOOK38)) == 150
    print("OK", len(BOOK38), "max_open_freq", max(c.values()))


if __name__ == "__main__":
    main()
